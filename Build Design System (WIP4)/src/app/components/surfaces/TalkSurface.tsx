function BranchPromptButton({ obs, color, breath, delay, onSelect }: {
  obs: Observation;
  color: string;
  breath: number;
  delay: number;
  onSelect: (obs: Observation) => void;
}) {
  const [revealed, setRevealed] = useState(0);
  const [selected, setSelected] = useState(false);
  const timerRef = useRef<ReturnType<typeof setTimeout>[]>([]);
  const breathPhase = Math.sin(breath * Math.PI * 2);

  // Use a seed word instead of the full opening — short, intimate, typed-back
  const seed = useMemo(() => {
    const used = new Set<string>();
    return pickSeed(obs.territory, used);
  }, [obs.territory]);

  useEffect(() => {
    timerRef.current.forEach(clearTimeout);
    timerRef.current = [];
    setRevealed(0);
    setSelected(false);

    let charIdx = 0;
    const typeNext = () => {
      if (charIdx >= seed.length) return;
      const ch = seed[charIdx];
      charIdx++;
      const idx = charIdx;
      setRevealed(idx);
      if (charIdx < seed.length) {
        const t = setTimeout(typeNext, charDelay(ch));
        timerRef.current.push(t);
      }
    };

    const startTimer = setTimeout(typeNext, delay * 1000);
    timerRef.current.push(startTimer);

    return () => { timerRef.current.forEach(clearTimeout); };
  }, [seed, delay]);

  const handleClick = () => {
    if (selected || revealed < 1) return;
    setSelected(true);
    setRevealed(seed.length);
    setTimeout(() => onSelect(obs), 400);
  };

  const visibleText = seed.slice(0, revealed);
  const isFullyTyped = revealed >= seed.length;
  const showCursor = !selected && revealed > 0 && !isFullyTyped;

  return (
    <motion.button
      className="cursor-pointer bg-transparent border-none outline-none"
      style={{
        padding: '12px 16px',
        textAlign: 'center',
        pointerEvents: revealed > 0 ? 'auto' : 'none',
      }}
      initial={{ opacity: 0 }}
      animate={{
        opacity: revealed > 0
          ? (selected ? 0.85 : op.voice + breathPhase * 0.03)
          : 0,
        scale: selected ? 1.04 : 1,
      }}
      whileHover={{ opacity: op.strong }}
      transition={{
        opacity: { duration: 0.4 },
        scale: { duration: 0.3, ease: [0.16, 1, 0.3, 1] },
      }}
      onClick={handleClick}
    >
      {/* INVITATION voice — serif italic, room.fg (neutral, not territory-colored) */}
      <span style={{
        fontFamily: SERIF,
        fontSize: typeSize.prose,
        fontWeight: weight.light,
        fontStyle: 'italic',
        color: room.fg,
        letterSpacing: tracking.body,
        lineHeight: leading.relaxed,
        position: 'relative',
      }}>
        {visibleText}
        {showCursor && (
          <span
            style={{
              display: 'inline-block',
              width: 1,
              height: '1em',
              backgroundColor: room.fg,
              marginLeft: 2,
              verticalAlign: 'text-bottom',
              opacity: 0.25 + breathPhase * 0.1,
              animation: 'typewriterCursorBlink 1s ease-in-out infinite',
            }}
          />
        )}
      </span>
      {/* Territory dot — subtle color signal */}
      {isFullyTyped && !selected && (
        <motion.span
          className="inline-block rounded-full"
          style={{
            width: 3, height: 3,
            backgroundColor: color,
            marginLeft: 8,
            verticalAlign: 'middle',
          }}
          initial={{ opacity: 0, scale: 0 }}
          animate={{ opacity: op.quiet, scale: 1 }}
          transition={{ duration: 0.5 }}
        />
      )}
    </motion.button>
  );
}

