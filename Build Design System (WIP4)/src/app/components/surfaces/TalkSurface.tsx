// CEREMONY SHIMMER — luminous motes behind naming ceremony
// ═════════════════════════════���═════════════════════

function CeremonyShimmer({ color, isUniverse }: { color: string; isUniverse?: boolean }) {
  const motes = useMemo(() => {
    const count = isUniverse ? 16 : 10;
    return Array.from({ length: count }, (_, i) => ({
      id: i,
      x: 15 + Math.random() * 70,
      y: 10 + Math.random() * 80,
      drift: (Math.random() - 0.5) * 12,
      size: 1 + Math.random() * (isUniverse ? 2.5 : 1.5),
      delay: Math.random() * 3,
      duration: 4 + Math.random() * 4,
    }));
  }, [isUniverse]);

  return (
    <div className="absolute inset-0 pointer-events-none overflow-hidden" style={{ zIndex: layer.base }}>
      {motes.map(m => (
        <motion.div
          key={m.id}
          className="absolute rounded-full"
          style={{
            left: `${m.x}%`, top: `${m.y}%`,
            width: m.size, height: m.size,
            background: color,
            boxShadow: `0 0 ${m.size * 6}px ${color}25`,
          }}
          initial={{ opacity: 0, y: 0 }}
          animate={{
            opacity: [0, 0.4, 0.2, 0],
            y: [0, m.drift, m.drift * 1.5],
            x: [0, m.drift * 0.3, m.drift * 0.6],
          }}
          transition={{
            delay: m.delay,
            duration: m.duration,
            ease: 'easeInOut',
            repeat: Infinity,
            repeatDelay: 1.5,
          }}
        />
      ))}
    </div>
  );
}

