function GuruWhisperItem({ fragment, color, breath, totalCount }: {
  fragment: GuruWhisperFragment;
  color: string;
  breath: number;
  totalCount?: number;
}) {
  // Phase U: Harmonic breathing — each whisper breathes at a phase offset
  // so simultaneous whispers pulse in rhythmic counterpoint, not unison
  const harmonicOffset = (fragment.harmonicIndex || 0) * (Math.PI * 2 / Math.max(totalCount || 1, 2));
  const breathPhase = Math.sin(breath * Math.PI * 2 + harmonicOffset);
  const [displayed, setDisplayed] = useState('');
  const [started, setStarted] = useState(false);
  const [complete, setComplete] = useState(false);
  const [fading, setFading] = useState(false);

  // Delay before typewriter begins
  useEffect(() => {
    const t = setTimeout(() => setStarted(true), fragment.delay * 1000);
    return () => clearTimeout(t);
  }, [fragment.delay]);

  // Typewriter effect — slower than prompts, more contemplative
  useEffect(() => {
    if (!started) return;
    let i = 0;
    const interval = setInterval(() => {
      i++;
      if (i <= fragment.text.length) {
        setDisplayed(fragment.text.slice(0, i));
      } else {
        setComplete(true);
        clearInterval(interval);
      }
    }, 45);
    return () => clearInterval(interval);
  }, [started, fragment.text]);

  // Fade out after lingering (complete + 4s)
  useEffect(() => {
    if (!complete) return;
    const t = setTimeout(() => setFading(true), 4000);
    return () => clearTimeout(t);
  }, [complete]);

  // Gentle vertical drift — 12px upward over the lifetime
  const driftY = started ? -12 : 0;

  return (
    <motion.div
      initial={{ opacity: 0, y: 0 }}
      animate={{
        opacity: fading ? 0 : (started ? op.ghost + breathPhase * 0.02 : 0),
        y: fading ? driftY - 8 : driftY,
      }}
      transition={{
        opacity: { duration: fading ? 2.5 : 1.5, ease: [0.16, 1, 0.3, 1] },
        y: { duration: 8, ease: 'linear' },
      }}
      style={{
        position: 'absolute',
        left: `${fragment.xBias * 100}%`,
        top: `${fragment.yBias * 100}%`,
        transform: 'translate(-50%, -50%)',
        maxWidth: '70%',
        textAlign: 'center',
      }}
    >
      <span style={{
        fontFamily: SERIF,
        fontSize: 'clamp(12px, 3vw, 15px)',
        fontWeight: weight.light,
        fontStyle: 'italic',
        lineHeight: leading.relaxed,
        color,
        // Harmonic layering: each successive echo is softer (0.55, 0.4, 0.28)
        opacity: fragment.harmonicIndex === 2 ? 0.28 : fragment.harmonicIndex === 1 ? 0.4 : 0.55,
      }}>
        {displayed}
        {started && !complete && (
          <span style={{
            display: 'inline-block',
            width: 1,
            height: '0.85em',
            backgroundColor: color,
            marginLeft: 1,
            verticalAlign: 'text-bottom',
            opacity: 0.4,
            animation: 'typewriterCursorBlink 1s ease-in-out infinite',
          }} />
        )}
      </span>
    </motion.div>
  );
}

// ═══════════════════════════════════════════════════
// WHISPER TRAIL — Phase T/U luminous thread + particles
// ═══════════════════════════════════════════════════
//
// A faint curved path drawn inside the 0..1 viewBox SVG
// that connects a whisper fragment's position to the
// illuminated star. Phase U adds luminous particles that
// drift along the bezier from whisper toward star, like
// fireflies tracing the invisible thread of connection.
// Bridge whispers get brighter trails with more particles.

/** Evaluate a quadratic bezier at parameter t (0..1) */
function bezierPoint(
  x0: number, y0: number,
  cx: number, cy: number,
  x1: number, y1: number,
  t: number,
): { x: number; y: number } {
  const u = 1 - t;
  return {
    x: u * u * x0 + 2 * u * t * cx + t * t * x1,
    y: u * u * y0 + 2 * u * t * cy + t * t * y1,
  };
}

function WhisperTrail({ fromX, fromY, toX, toY, color, delay, breath, isBridge }: {
  fromX: number;
  fromY: number;
  toX: number;
  toY: number;
  color: string;
  delay: number;
  breath: number;
  isBridge?: boolean;
}) {
  const [visible, setVisible] = useState(false);
  const [fading, setFading] = useState(false);
  // Phase U: particle animation progress (0..1 cycling)
  const [particleT, setParticleT] = useState(0);

  useEffect(() => {
    const t = setTimeout(() => setVisible(true), (delay + 1.5) * 1000);
    return () => clearTimeout(t);
  }, [delay]);

  useEffect(() => {
    if (!visible) return;
    const t = setTimeout(() => setFading(true), 6000);
    return () => clearTimeout(t);
  }, [visible]);

  // Phase U: animate particles along the trail
  useEffect(() => {
    if (!visible || fading) return;
    let frame: number;
    let start: number | null = null;
    const duration = isBridge ? 4000 : 5000; // bridge particles move faster
    const tick = (ts: number) => {
      if (!start) start = ts;
      const elapsed = (ts - start) % duration;
      setParticleT(elapsed / duration);
      frame = requestAnimationFrame(tick);
    };
    frame = requestAnimationFrame(tick);
    return () => cancelAnimationFrame(frame);
  }, [visible, fading, isBridge]);

  if (!visible && !fading) return null;

  // Quadratic bezier control point: midpoint with perpendicular offset
  const midX = (fromX + toX) / 2;
  const midY = (fromY + toY) / 2;
  const dx = toX - fromX;
  const dy = toY - fromY;
  const perpX = -dy * 0.15;
  const perpY = dx * 0.15;
  const cpX = midX + perpX;
  const cpY = midY + perpY;

  const breathOp = Math.sin(breath * Math.PI * 2) * 0.02;
  // Bridge whispers get brighter trails
  const trailIntensity = isBridge ? 0.12 : 0.08;
  const baseOpacity = fading ? 0 : trailIntensity + breathOp;

  const d = `M ${fromX} ${fromY} Q ${cpX} ${cpY} ${toX} ${toY}`;

  // Phase U: compute particle positions along the bezier
  const particleCount = isBridge ? 3 : 2;
  const particles: { x: number; y: number; op: number }[] = [];
  if (visible && !fading) {
    for (let i = 0; i < particleCount; i++) {
      // Stagger particles evenly along the curve, cycling
      const t = (particleT + i / particleCount) % 1;
      const p = bezierPoint(fromX, fromY, cpX, cpY, toX, toY, t);
      // Particles fade at endpoints, brightest at mid-journey
      const edgeFade = Math.sin(t * Math.PI);
      particles.push({ x: p.x, y: p.y, op: edgeFade * (isBridge ? 0.35 : 0.2) });
    }
  }

  return (
    <g>
      <path
        d={d}
        fill="none"
        stroke={color}
        strokeWidth={isBridge ? 0.0015 : 0.001}
        opacity={baseOpacity}
        style={{
          transition: `opacity ${fading ? 2.5 : 1.5}s cubic-bezier(0.16, 1, 0.3, 1)`,
        }}
      />
      {particles.map((p, i) => (
        <circle
          key={`particle-${i}`}
          cx={p.x}
          cy={p.y}
          r={isBridge ? 0.003 : 0.002}
          fill={color}
          opacity={p.op}
        />
      ))}
    </g>
  );
}

