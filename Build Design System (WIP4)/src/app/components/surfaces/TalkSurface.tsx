function UniverseCanvas({ stars, camera, width, height, breath, focusedStarId, dimmed, constellationNames = {}, onTerritoryProximity, cosmicSeason, sessionStarIds = [], territoryBreaths = [], passageLuminances = [], afterglows = [], inscriptionRipples = [], passageTides = [], stardustMemories = [], harmonicPairs = [], gravityWells = [], passageWeaves = [], weaveGlowWords = [], weaveGlowStartTime = 0 }: UniverseCanvasProps) {
  const canvasRef = useRef<HTMLCanvasElement>(null);
  const rafRef = useRef(0);

  const parseHex = (c: string) => {
    const h = c.replace('#', '');
    return [parseInt(h.slice(0, 2), 16), parseInt(h.slice(2, 4), 16), parseInt(h.slice(4, 6), 16)];
  };

  // ── Shooting star state (persistent across frames) ──
  const shootRef = useRef<{
    active: boolean; x: number; y: number;
    angle: number; speed: number; birth: number; life: number;
  }>({ active: false, x: 0, y: 0, angle: 0, speed: 0, birth: 0, life: 0 });

  // ── Camera delta tracking for orbit trails ──
  const prevCameraRef = useRef<{ yaw: number; pitch: number }>({ yaw: 0, pitch: 0 });

  // ── Star bloom animation state ──
  // Tracks stars that recently illuminated for bloom effect
  type BloomState = {
    starId: string; startTime: number;
    x: number; y: number; cr: number; cg: number; cb: number;
  };
  const bloomsRef = useRef<BloomState[]>([]);
  // Track previous illumination state to detect new illuminations
  const prevIllumRef = useRef<Set<string>>(new Set());

  // ── Constellation reveal choreography ──
  // Track which constellations have been revealed this session
  const revealedRef = useRef<Map<string, number>>(new Map()); // conId → reveal timestamp

  // ── Constellation completion particles ──
  type CompletionParticle = {
    x: number; y: number; vx: number; vy: number;
    birth: number; life: number; size: number;
    cr: number; cg: number; cb: number;
  };
  const completionParticlesRef = useRef<CompletionParticle[]>([]);
  const completedSetRef = useRef<Set<string>>(new Set());

  // Seed completed set and prevIllum from persisted state — prevents re-bursting on reload
  const seededRef = useRef(false);
  useEffect(() => {
    if (seededRef.current) return;
    const alreadyComplete = CONSTELLATIONS.filter(c =>
      c.starIds.every(sid => stars.find(s => s.id === sid)?.illuminated)
    );
    if (alreadyComplete.length > 0 || stars.some(s => s.illuminated)) {
      for (const c of alreadyComplete) completedSetRef.current.add(c.id);
      // Seed previous illumination state so we don't bloom on load
      for (const s of stars) {
        if (s.illuminated) prevIllumRef.current.add(s.id);
      }
      // Seed revealed constellations — already-visited ones don't re-animate
      for (const con of CONSTELLATIONS) {
        if (stars.some(s => s.constellation === con.id && s.illuminated)) {
          revealedRef.current.set(con.id, 0); // 0 = already revealed (no animation needed)
        }
      }
      seededRef.current = true;
    }
  }, [stars]);

  const render = useCallback(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext('2d');
    if (!ctx) return;

    const dpr = window.devicePixelRatio || 1;
    const cw = Math.round(width * dpr);
    const ch = Math.round(height * dpr);
    if (canvas.width !== cw || canvas.height !== ch) { canvas.width = cw; canvas.height = ch; }

    ctx.save();
    ctx.scale(dpr, dpr);
    ctx.clearRect(0, 0, width, height);

    const t = Date.now() * 0.001;
    const breathCycle = Math.sin(breath * Math.PI * 2);
    const globalA = dimmed ? 0.25 : 1;

    // ── Origin aurora — evolves with illumination progress ──
    const totalLit = stars.filter(s => s.illuminated).length;
    const progress = totalLit / stars.length; // 0 to 1
    const auroraR = Math.min(width, height) * (0.25 + progress * 0.15);
    const auroraBase = 0.012 + progress * 0.018;
    const auroraBreathAmp = 0.004 + progress * 0.006;
    const aurora = ctx.createRadialGradient(width / 2, height / 2, 0, width / 2, height / 2, auroraR);
    const auroraBreath = auroraBase + Math.sin(t * 0.15) * auroraBreathAmp;
    // Shift from cool violet toward warm gold as more stars illuminate
    const aR = Math.round(138 + progress * 80);
    const aG = Math.round(122 + progress * 60);
    const aB = Math.round(255 - progress * 100);
    aurora.addColorStop(0, `rgba(${aR},${aG},${aB},${(auroraBreath * globalA).toFixed(4)})`);
    aurora.addColorStop(0.4, `rgba(${aR},${aG},${aB},${(auroraBreath * 0.3 * globalA).toFixed(4)})`);
    aurora.addColorStop(1, `rgba(${aR},${aG},${aB},0)`);
    ctx.fillStyle = aurora;
    ctx.fillRect(0, 0, width, height);

    // ── Atmospheric vignette — softens edges, eases with progress ──
    const vignetteA = Math.max(0.02, 0.08 - progress * 0.04);
    const vignette = ctx.createRadialGradient(
      width / 2, height / 2, Math.min(width, height) * 0.3,
      width / 2, height / 2, Math.max(width, height) * 0.7,
    );
    vignette.addColorStop(0, 'rgba(0,0,0,0)');
    vignette.addColorStop(1, `rgba(0,0,0,${vignetteA.toFixed(3)})`);
    ctx.fillStyle = vignette;
    ctx.fillRect(0, 0, width, height);

    // ── Secondary nebula bands — warm drift emerging with progress ──
    if (progress > 0.15 && !dimmed) {
      const nebulaIntensity = Math.min(1, (progress - 0.15) / 0.6);
      // Asymmetric warm drift — upper right
      const nx = width * 0.65 + Math.sin(t * 0.03) * width * 0.05;
      const ny = height * 0.35 + Math.cos(t * 0.025) * height * 0.04;
      const nr = Math.min(width, height) * (0.15 + nebulaIntensity * 0.1);
      const nebula2 = ctx.createRadialGradient(nx, ny, 0, nx, ny, nr);
      const nA = 0.006 * nebulaIntensity;
      nebula2.addColorStop(0, `rgba(255,200,140,${nA.toFixed(4)})`);
      nebula2.addColorStop(0.5, `rgba(200,160,220,${(nA * 0.5).toFixed(4)})`);
      nebula2.addColorStop(1, 'rgba(200,160,220,0)');
      ctx.fillStyle = nebula2;
      ctx.fillRect(0, 0, width, height);

      // Lower left counterpoint — cooler
      if (progress > 0.35) {
        const n2x = width * 0.3 + Math.cos(t * 0.02) * width * 0.04;
        const n2y = height * 0.7 + Math.sin(t * 0.035) * height * 0.03;
        const n2r = Math.min(width, height) * 0.12;
        const nebula3 = ctx.createRadialGradient(n2x, n2y, 0, n2x, n2y, n2r);
        const n2A = 0.004 * nebulaIntensity;
        nebula3.addColorStop(0, `rgba(140,180,255,${n2A.toFixed(4)})`);
        nebula3.addColorStop(0.6, `rgba(140,140,200,${(n2A * 0.4).toFixed(4)})`);
        nebula3.addColorStop(1, 'rgba(140,140,200,0)');
        ctx.fillStyle = nebula3;
        ctx.fillRect(0, 0, width, height);
      }
    }

    // ── Universe completion glow — when all stars are illuminated ──
    if (progress >= 1 && !dimmed) {
      const completionPulse = Math.sin(t * 0.08) * 0.5 + 0.5;
      const compR = Math.min(width, height) * 0.5;
      const compGlow = ctx.createRadialGradient(width / 2, height / 2, 0, width / 2, height / 2, compR);
      const compA = 0.015 + completionPulse * 0.01;
      compGlow.addColorStop(0, `rgba(255,224,136,${compA.toFixed(4)})`);
      compGlow.addColorStop(0.3, `rgba(218,182,155,${(compA * 0.5).toFixed(4)})`);
      compGlow.addColorStop(0.6, `rgba(138,122,255,${(compA * 0.3).toFixed(4)})`);
      compGlow.addColorStop(1, 'rgba(138,122,255,0)');
      ctx.fillStyle = compGlow;
      ctx.fillRect(0, 0, width, height);
    }

    // ── Phase F Item 24: Cosmic season visual effects ──
    if (cosmicSeason && cosmicSeason.intensity > 0 && !dimmed) {
      const si = cosmicSeason.intensity;
      if (cosmicSeason.effect === 'solstice_glow') {
        // Warm golden wash from below (summer) or cool silver from above (winter)
        const isSummer = cosmicSeason.name.includes('summer');
        const solGrad = ctx.createLinearGradient(0, isSummer ? height : 0, 0, isSummer ? 0 : height);
        if (isSummer) {
          solGrad.addColorStop(0, `rgba(255,200,100,${(0.012 * si * globalA).toFixed(4)})`);
          solGrad.addColorStop(0.4, `rgba(255,180,80,${(0.005 * si * globalA).toFixed(4)})`);
        } else {
          solGrad.addColorStop(0, `rgba(180,200,255,${(0.010 * si * globalA).toFixed(4)})`);
          solGrad.addColorStop(0.4, `rgba(160,180,240,${(0.004 * si * globalA).toFixed(4)})`);
        }
        solGrad.addColorStop(1, 'rgba(0,0,0,0)');
        ctx.fillStyle = solGrad;
        ctx.fillRect(0, 0, width, height);
      } else if (cosmicSeason.effect === 'equinox_aurora') {
        // Dual aurora bands from both poles
        const eqPulse = Math.sin(t * 0.06) * 0.3 + 0.7;
        const eqA = 0.008 * si * eqPulse * globalA;
        const topAurora = ctx.createRadialGradient(width * 0.4, 0, 0, width * 0.4, 0, height * 0.5);
        topAurora.addColorStop(0, `rgba(100,255,180,${eqA.toFixed(4)})`);
        topAurora.addColorStop(0.5, `rgba(140,180,255,${(eqA * 0.5).toFixed(4)})`);
        topAurora.addColorStop(1, 'rgba(0,0,0,0)');
        ctx.fillStyle = topAurora;
        ctx.fillRect(0, 0, width, height);
        const botAurora = ctx.createRadialGradient(width * 0.6, height, 0, width * 0.6, height, height * 0.4);
        botAurora.addColorStop(0, `rgba(200,140,255,${(eqA * 0.7).toFixed(4)})`);
        botAurora.addColorStop(1, 'rgba(0,0,0,0)');
        ctx.fillStyle = botAurora;
        ctx.fillRect(0, 0, width, height);
      } else if (cosmicSeason.effect === 'new_moon') {
        // Deeper void — strengthen vignette, sharpen star contrast
        const nmVignette = ctx.createRadialGradient(
          width / 2, height / 2, Math.min(width, height) * 0.2,
          width / 2, height / 2, Math.max(width, height) * 0.65,
        );
        nmVignette.addColorStop(0, 'rgba(0,0,0,0)');
        nmVignette.addColorStop(1, `rgba(0,0,0,${(0.06 * si).toFixed(3)})`);
        ctx.fillStyle = nmVignette;
        ctx.fillRect(0, 0, width, height);
      } else if (cosmicSeason.effect === 'full_moon') {
        // Soft ambient lift — subtle brightening across the field
        const fmGlow = ctx.createRadialGradient(
          width * 0.7, height * 0.2, 0,
          width * 0.7, height * 0.2, Math.min(width, height) * 0.6,
        );
        fmGlow.addColorStop(0, `rgba(220,225,240,${(0.008 * si * globalA).toFixed(4)})`);
        fmGlow.addColorStop(0.5, `rgba(200,210,230,${(0.003 * si * globalA).toFixed(4)})`);
        fmGlow.addColorStop(1, 'rgba(0,0,0,0)');
        ctx.fillStyle = fmGlow;
        ctx.fillRect(0, 0, width, height);
      }
      // meteor_shower effect is handled in the shooting star section below (increased spawn rate)
    }

    // ── Deep field dust (three parallax layers) ──
    for (let layer = 0; layer < 3; layer++) {
      const count = [50, 30, 15][layer];
      const speed = [0.008, 0.015, 0.025][layer];
      const size = [0.3, 0.5, 0.7][layer];
      const alpha = [0.008, 0.012, 0.018][layer] * globalA;
      for (let i = 0; i < count; i++) {
        const seed = i * 73.137 + layer * 1000;
        const ax = ((Math.sin(t * speed + seed) + 1) / 2) * width;
        const ay = ((Math.cos(t * speed * 0.7 + seed * 0.7) + 1) / 2) * height;
        ctx.beginPath();
        ctx.arc(ax, ay, size, 0, Math.PI * 2);
        ctx.fillStyle = `rgba(180,190,220,${alpha.toFixed(4)})`;
        ctx.fill();
      }
    }

    // ── Project stars ──
    const projected: { id: string; x: number; y: number; scale: number; star: UniverseStar; behind: boolean }[] = [];
    for (const star of stars) {
      const p = project(star.pos, camera, width, height);
      projected.push({ id: star.id, x: p.x, y: p.y, scale: p.scale, star, behind: p.behind });
    }
    projected.sort((a, b) => a.scale - b.scale);

    // ── Item 14: Territory atmosphere layers — multi-layer atmospheric wash per constellation ──
    // ── Item 16: Localized nebulae — organic drifting clouds per constellation ──
    for (const constellation of CONSTELLATIONS) {
      const conStars = projected.filter(p => p.star.constellation === constellation.id);
      const litCount = conStars.filter(p => p.star.illuminated).length;
      const totalCount = constellation.starIds.length;
      if (litCount === 0 && !dimmed) continue;

      const cp = project(constellation.center, camera, width, height);
      if (cp.behind || cp.scale < 0.15) continue;

      const [cr, cg, cb] = parseHex(constellation.color);
      const conProgress = litCount / totalCount;

      // ─ Layer 1: Deep atmosphere — large, soft base wash ─
      const deepR = Math.max(60, 120 * cp.scale * (1 + conProgress * 0.3));
      const deepA = (0.006 + conProgress * 0.012) * cp.scale * globalA;
      const deepNeb = ctx.createRadialGradient(cp.x, cp.y, 0, cp.x, cp.y, deepR);
      deepNeb.addColorStop(0, `rgba(${cr},${cg},${cb},${Math.min(0.04, deepA).toFixed(4)})`);
      deepNeb.addColorStop(0.4, `rgba(${cr},${cg},${cb},${(deepA * 0.5).toFixed(4)})`);
      deepNeb.addColorStop(0.7, `rgba(${cr},${cg},${cb},${(deepA * 0.15).toFixed(4)})`);
      deepNeb.addColorStop(1, `rgba(${cr},${cg},${cb},0)`);
      ctx.fillStyle = deepNeb;
      ctx.beginPath(); ctx.arc(cp.x, cp.y, deepR, 0, Math.PI * 2); ctx.fill();

      // ─ Layer 2: Mid atmosphere — warm inner glow, shifts with progress ─
      if (conProgress > 0.2) {
        const midIntensity = (conProgress - 0.2) / 0.8;
        const midR = Math.max(30, 55 * cp.scale);
        const midA = 0.008 * midIntensity * cp.scale * globalA;
        // Warm shift: blend toward white at center
        const warmR = Math.min(255, cr + Math.round(40 * midIntensity));
        const warmG = Math.min(255, cg + Math.round(30 * midIntensity));
        const warmB = Math.min(255, cb + Math.round(15 * midIntensity));
        const midNeb = ctx.createRadialGradient(cp.x, cp.y, 0, cp.x, cp.y, midR);
        midNeb.addColorStop(0, `rgba(${warmR},${warmG},${warmB},${midA.toFixed(4)})`);
        midNeb.addColorStop(0.5, `rgba(${cr},${cg},${cb},${(midA * 0.4).toFixed(4)})`);
        midNeb.addColorStop(1, `rgba(${cr},${cg},${cb},0)`);
        ctx.fillStyle = midNeb;
        ctx.beginPath(); ctx.arc(cp.x, cp.y, midR, 0, Math.PI * 2); ctx.fill();
      }

      // ─ Layer 3: Localized nebula wisps — organic drifting clouds ─
      // Multiple overlapping elliptical gradients that orbit slowly
      if (!dimmed && litCount > 0) {
        const wispCount = Math.min(3, 1 + Math.floor(conProgress * 2));
        for (let wi = 0; wi < wispCount; wi++) {
          const wispAngle = (wi / wispCount) * Math.PI * 2 + t * (0.02 + wi * 0.008);
          const wispDist = (25 + wi * 18) * cp.scale;
          const wispX = cp.x + Math.cos(wispAngle) * wispDist;
          const wispY = cp.y + Math.sin(wispAngle + wi * 0.3) * wispDist * 0.7;
          const wispR = (15 + wi * 8 + conProgress * 12) * cp.scale;
          const wispBreath = Math.sin(t * 0.12 + wi * 2.1) * 0.3 + 0.7;
          const wispA = (0.005 + conProgress * 0.008) * cp.scale * globalA * wispBreath;

          if (wispA > 0.001) {
            ctx.save();
            ctx.translate(wispX, wispY);
            ctx.rotate(wispAngle * 0.3);
            ctx.scale(1.4, 0.8); // elliptical
            const wispGrad = ctx.createRadialGradient(0, 0, 0, 0, 0, wispR);
            wispGrad.addColorStop(0, `rgba(${cr},${cg},${cb},${wispA.toFixed(4)})`);
            wispGrad.addColorStop(0.6, `rgba(${cr},${cg},${cb},${(wispA * 0.3).toFixed(4)})`);
            wispGrad.addColorStop(1, `rgba(${cr},${cg},${cb},0)`);
            ctx.fillStyle = wispGrad;
            ctx.beginPath(); ctx.arc(0, 0, wispR, 0, Math.PI * 2); ctx.fill();
            ctx.restore();
          }
        }
      }

      // ─ Layer 4: Outer haze — cool desaturated edge that forms territorial boundary ─
      if (conProgress > 0.4 && !dimmed) {
        const hazeIntensity = (conProgress - 0.4) / 0.6;
        const hazeR = Math.max(80, 160 * cp.scale);
        const hazeA = 0.003 * hazeIntensity * cp.scale * globalA;
        // Desaturated version of constellation color
        const hazeColorR = Math.round(cr * 0.5 + 128 * 0.5);
        const hazeColorG = Math.round(cg * 0.5 + 128 * 0.5);
        const hazeColorB = Math.round(cb * 0.5 + 128 * 0.5);
        const hazeGrad = ctx.createRadialGradient(cp.x, cp.y, hazeR * 0.4, cp.x, cp.y, hazeR);
        hazeGrad.addColorStop(0, `rgba(${hazeColorR},${hazeColorG},${hazeColorB},${hazeA.toFixed(4)})`);
        hazeGrad.addColorStop(1, `rgba(${hazeColorR},${hazeColorG},${hazeColorB},0)`);
        ctx.fillStyle = hazeGrad;
        ctx.beginPath(); ctx.arc(cp.x, cp.y, hazeR, 0, Math.PI * 2); ctx.fill();
      }
    }

    // ── Item 26: Territory boundary sensing — detect camera facing a constellation ──
    {
      let closestCon: string | null = null;
      let closestDist = Infinity;
      const screenCx = width / 2;
      const screenCy = height / 2;
      const boundaryThreshold = Math.min(width, height) * 0.3;

      for (const constellation of CONSTELLATIONS) {
        const litCount = stars.filter(s => s.constellation === constellation.id && s.illuminated).length;
        if (litCount === 0) continue;

        const cp = project(constellation.center, camera, width, height);
        if (cp.behind) continue;

        const dist = Math.sqrt((cp.x - screenCx) ** 2 + (cp.y - screenCy) ** 2);
        if (dist < boundaryThreshold && dist < closestDist) {
          closestDist = dist;
          closestCon = constellation.id;
        }
      }

      // Render territory boundary wash when camera faces a constellation
      if (closestCon && !dimmed) {
        const con = CONSTELLATIONS.find(c => c.id === closestCon)!;
        const [bcr, bcg, bcb] = parseHex(con.color);
        const proximity = 1 - (closestDist / boundaryThreshold); // 0 = edge, 1 = centered
        const washA = proximity * 0.012 * globalA;

        // Full-screen subtle atmospheric wash
        const boundaryWash = ctx.createRadialGradient(
          screenCx, screenCy, 0,
          screenCx, screenCy, Math.max(width, height) * 0.5,
        );
        boundaryWash.addColorStop(0, `rgba(${bcr},${bcg},${bcb},${washA.toFixed(4)})`);
        boundaryWash.addColorStop(0.5, `rgba(${bcr},${bcg},${bcb},${(washA * 0.3).toFixed(4)})`);
        boundaryWash.addColorStop(1, `rgba(${bcr},${bcg},${bcb},0)`);
        ctx.fillStyle = boundaryWash;
        ctx.fillRect(0, 0, width, height);
      }

      // Report territory proximity for UI layer
      if (onTerritoryProximity) {
        onTerritoryProximity(closestCon);
      }
    }

    // ── Detect new star illuminations → trigger bloom ──
    const currentIllum = new Set(stars.filter(s => s.illuminated).map(s => s.id));
    for (const sid of currentIllum) {
      if (!prevIllumRef.current.has(sid)) {
        // New illumination detected — spawn bloom
        const star = stars.find(s => s.id === sid);
        const con = star ? CONSTELLATIONS.find(c => c.starIds.includes(sid)) : null;
        const p = star ? project(star.pos, camera, width, height) : null;
        if (p && !p.behind && con) {
          const [bcr, bcg, bcb] = parseHex(con.color);
          bloomsRef.current.push({
            starId: sid, startTime: t,
            x: p.x, y: p.y, cr: bcr, cg: bcg, cb: bcb,
          });
        }
      }
    }
    prevIllumRef.current = currentIllum;

    // ── Constellation threads — progressive line evolution + territory completion evolution ──
    // Item 13: 0 lit = invisible, 1 lit = faintest dotted, 3+ = partial glow, all = full pulse
    // Phase E Item 19: Completed territories evolve visually
    for (const constellation of CONSTELLATIONS) {
      const [cr, cg, cb] = parseHex(constellation.color);
      const conStars = stars.filter(s => s.constellation === constellation.id);
      const litCount = conStars.filter(s => s.illuminated).length;
      const totalCount = constellation.starIds.length;
      const evo = computeTerritoryEvolution(constellation.id, stars);

      // 0 lit = invisible
      if (litCount === 0) continue;

      // Constellation reveal choreography (item 17)
      // First time this constellation's lines appear this session, animate the drawing
      let revealProgress = 1; // default: fully drawn
      if (!revealedRef.current.has(constellation.id)) {
        // First reveal this session — start animation
        revealedRef.current.set(constellation.id, t);
      }
      const revealStart = revealedRef.current.get(constellation.id)!;
      if (revealStart > 0) {
        // Animate over 1.5s
        revealProgress = Math.min(1, (t - revealStart) / 1.5);
      }

      // How many connections to draw based on reveal progress
      const totalConns = constellation.connections.length;
      const connsToShow = revealStart > 0
        ? Math.ceil(revealProgress * totalConns)
        : totalConns;

      for (let ci = 0; ci < Math.min(connsToShow, totalConns); ci++) {
        const [idA, idB] = constellation.connections[ci];
        const a = projected.find(p => p.id === idA);
        const b = projected.find(p => p.id === idB);
        if (!a || !b || a.behind || b.behind) continue;

        const bothLit = a.star.illuminated && b.star.illuminated;
        const eitherLit = a.star.illuminated || b.star.illuminated;
        const allComplete = litCount === totalCount;

        // Progressive alpha based on constellation illumination + territory evolution
        let baseAlpha: number;
        let lineW: number;
        if (allComplete) {
          // Evolved: pulsing lines with evolution-driven amplitude
          const pulse = 0.85 + Math.sin(t * (0.4 + evo.breathAmp * 2)) * (0.15 + evo.breathAmp);
          baseAlpha = 0.12 * pulse * evo.glowMul * 0.6;
          lineW = 0.6 + evo.breathAmp * 2;
        } else if (bothLit) {
          baseAlpha = 0.06 * evo.glowMul * 0.7;
          lineW = 0.5;
        } else if (litCount >= 3) {
          // Flowing lines
          baseAlpha = eitherLit ? 0.035 : 0.015;
          lineW = 0.4;
        } else {
          // 1-2 lit: faintest dotted line
          baseAlpha = eitherLit ? 0.02 : 0.008;
          lineW = 0.3;
        }

        const lineAlpha = Math.min(a.scale, b.scale) * baseAlpha * globalA;
        if (lineAlpha < 0.002) continue;

        // For reveal animation: partial stroke on the last connection being drawn
        const isLastConn = revealStart > 0 && ci === connsToShow - 1 && revealProgress < 1;
        const connProgress = isLastConn
          ? (revealProgress * totalConns) - Math.floor(revealProgress * totalConns)
          : 1;

        const mx = (a.x + b.x) / 2 + Math.sin(t * 0.1 + a.x * 0.01) * 4;
        const my = (a.y + b.y) / 2 + Math.cos(t * 0.1 + b.y * 0.01) * 3;

        ctx.beginPath();
        ctx.moveTo(a.x, a.y);

        if (connProgress < 1) {
          // Partial draw — interpolate along the curve
          const ex = a.x + (b.x - a.x) * connProgress;
          const ey = a.y + (b.y - a.y) * connProgress;
          const emx = a.x + (mx - a.x) * connProgress;
          const emy = a.y + (my - a.y) * connProgress;
          ctx.quadraticCurveTo(emx, emy, ex, ey);
        } else {
          ctx.quadraticCurveTo(mx, my, b.x, b.y);
        }

        // Line dash style driven by territory evolution
        if (evo.lineStyle === 'dotted') {
          ctx.setLineDash([2, 4]);
        } else if (evo.lineStyle === 'flowing') {
          // Flowing: animated dash offset creates movement
          ctx.setLineDash([6, 3]);
          ctx.lineDashOffset = -t * 8;
        } else {
          ctx.setLineDash([]);
        }

        ctx.strokeStyle = `rgba(${cr},${cg},${cb},${lineAlpha.toFixed(4)})`;
        ctx.lineWidth = lineW;
        ctx.stroke();
        ctx.setLineDash([]); // reset
        ctx.lineDashOffset = 0;
      }
    }

    // ── Item 15: Depth-of-field scaling — stars get diffused at distance ──
    // Scale range: near camera (scale > 1.0) = sharp+bright, far (scale < 0.4) = soft+dim
    const dofNear = 1.0;   // scale threshold for "near" sharpness boost
    const dofFar = 0.35;   // scale threshold below which heavy diffusion
    const computeDepthFactor = (s: number) => {
      if (s >= dofNear) return 1.15; // slight brightness boost when very close
      if (s <= dofFar) return 0.35 + (s / dofFar) * 0.35; // significant dimming at distance
      // Linear interpolation in the mid range
      return 0.7 + ((s - dofFar) / (dofNear - dofFar)) * 0.3;
    };

    // ─��� Stars ──
    for (const p of projected) {
      if (p.behind || p.x < -50 || p.x > width + 50 || p.y < -50 || p.y > height + 50) continue;
      const { star, scale } = p;
      const constellation = CONSTELLATIONS.find(c => c.starIds.includes(star.id));
      const color = constellation?.color || '#AAB0C0';
      const [cr, cg, cb] = parseHex(color);

      const isFocused = star.id === focusedStarId;
      const isIlluminated = star.illuminated;
      const hasStardust = star.stardust.length > 0;
      const depthFactor = computeDepthFactor(scale);
      // Phase H: Passage luminance modulates star size and glow
      const lum = passageLuminances.find(l => l.starId === star.id);
      const lumFactor = lum ? lum.luminance : 0.5;
      const lumDeep = lum?.deep || false;
      const baseSize = star.magnitude * scale * 0.4 * (0.85 + depthFactor * 0.15) * (isIlluminated ? (0.85 + lumFactor * 0.3) : 1);
      const twinkle = (0.7 + Math.sin(t * (1.5 + star.pos.x * 0.003) + star.pos.y * 0.005) * 0.3) * depthFactor;
      // Phase H: Afterglow — warm aureole for recently inscribed stars
      const afterglow = afterglows.find(ag => ag.starId === star.id);

      // Check if this star is currently blooming (item 12)
      const bloom = bloomsRef.current.find(b => b.starId === star.id);
      const bloomAge = bloom ? t - bloom.startTime : -1;
      const isBlooming = bloomAge >= 0 && bloomAge < 2.0; // 2s bloom duration

      if (isFocused) {
        const glowR = Math.max(6, baseSize * 6 + breathCycle * 4);
        const grad = ctx.createRadialGradient(p.x, p.y, 0, p.x, p.y, glowR);
        grad.addColorStop(0, `rgba(${cr},${cg},${cb},${(0.18 * globalA).toFixed(3)})`);
        grad.addColorStop(0.3, `rgba(${cr},${cg},${cb},${(0.06 * globalA).toFixed(3)})`);
        grad.addColorStop(1, `rgba(${cr},${cg},${cb},0)`);
        ctx.fillStyle = grad;
        ctx.beginPath(); ctx.arc(p.x, p.y, glowR, 0, Math.PI * 2); ctx.fill();

        ctx.beginPath(); ctx.arc(p.x, p.y, Math.max(2, baseSize * 1.3), 0, Math.PI * 2);
        ctx.fillStyle = `rgba(${cr},${cg},${cb},${(0.65 * globalA).toFixed(3)})`;
        ctx.fill();

        ctx.beginPath(); ctx.arc(p.x, p.y, Math.max(8, baseSize * 4), 0, Math.PI * 2);
        ctx.strokeStyle = `rgba(${cr},${cg},${cb},${(0.06 * globalA).toFixed(3)})`;
        ctx.lineWidth = 0.4; ctx.stroke();
      } else if (isIlluminated) {
        // ── Star bloom animation (item 12) ──
        // Bloom sequence: point → rapid expansion (0→1.8) → settle (1.8→1.2) → sparks → stabilize
        let bloomScale = 1;
        let bloomGlowMul = 1;
        if (isBlooming) {
          const bp = bloomAge / 2.0; // 0 → 1 over 2s
          if (bp < 0.15) {
            // Rapid expansion: 0 → 1.8
            bloomScale = (bp / 0.15) * 1.8;
            bloomGlowMul = 1 + bp / 0.15 * 3;
          } else if (bp < 0.4) {
            // Settle: 1.8 → 1.2
            const settle = (bp - 0.15) / 0.25;
            bloomScale = 1.8 - settle * 0.6;
            bloomGlowMul = 4 - settle * 2.5;
          } else {
            // Stabilize: 1.2 → 1.0
            const stab = (bp - 0.4) / 0.6;
            bloomScale = 1.2 - stab * 0.2;
            bloomGlowMul = 1.5 - stab * 0.5;
          }
          // Update bloom screen position for sparks
          bloom!.x = p.x;
          bloom!.y = p.y;
        }

        // Phase H: Luminance-modulated glow — deeper passages radiate more warmly
        const lumGlow = 0.8 + lumFactor * 0.4; // 0.8 to 1.2
        const glowR = Math.max(4, baseSize * 3.5 * bloomScale * lumGlow);
        const grad = ctx.createRadialGradient(p.x, p.y, 0, p.x, p.y, glowR);
        const glowAlpha = (0.10 + lumFactor * 0.04) * twinkle * globalA * bloomGlowMul;
        grad.addColorStop(0, `rgba(${cr},${cg},${cb},${Math.min(0.6, glowAlpha).toFixed(3)})`);
        // Deep passages get a warmer mid-stop
        if (lumDeep && !isBlooming) {
          const warmMidR = Math.min(255, cr + 25);
          const warmMidG = Math.min(255, cg + 15);
          grad.addColorStop(0.4, `rgba(${warmMidR},${warmMidG},${cb},${(glowAlpha * 0.3).toFixed(3)})`);
        }
        grad.addColorStop(1, `rgba(${cr},${cg},${cb},0)`);
        ctx.fillStyle = grad;
        ctx.beginPath(); ctx.arc(p.x, p.y, glowR, 0, Math.PI * 2); ctx.fill();

        const coreSize = Math.max(1.5, baseSize * 0.9 * bloomScale);
        ctx.beginPath();
        ctx.arc(p.x, p.y, coreSize, 0, Math.PI * 2);
        const coreAlpha = (0.40 + lumFactor * 0.1) * twinkle * globalA * Math.min(2, bloomGlowMul);
        ctx.fillStyle = `rgba(${cr},${cg},${cb},${Math.min(0.9, coreAlpha).toFixed(3)})`;
        ctx.fill();

        // Phase H: Inscription afterglow — warm aureole for recently inscribed stars
        if (afterglow && afterglow.intensity > 0.05 && !isBlooming) {
          const agR = Math.max(8, baseSize * 5 * afterglow.intensity);
          const agPulse = Math.sin(t * 0.12 + afterglow.age * 0.01) * 0.15 + 0.85;
          const agA = afterglow.intensity * 0.06 * agPulse * globalA;
          const agGrad = ctx.createRadialGradient(p.x, p.y, 0, p.x, p.y, agR);
          // Warm-shifted color for the aureole
          const agCr = Math.min(255, cr + 30);
          const agCg = Math.min(255, cg + 20);
          const agCb = Math.min(255, cb + 5);
          agGrad.addColorStop(0, `rgba(${agCr},${agCg},${agCb},${agA.toFixed(4)})`);
          agGrad.addColorStop(0.4, `rgba(${cr},${cg},${cb},${(agA * 0.5).toFixed(4)})`);
          agGrad.addColorStop(1, `rgba(${cr},${cg},${cb},0)`);
          ctx.fillStyle = agGrad;
          ctx.beginPath(); ctx.arc(p.x, p.y, agR, 0, Math.PI * 2); ctx.fill();
        }

        // Bloom flash — bright white-hot center during expansion
        if (isBlooming && bloomAge < 0.5) {
          const flashA = (1 - bloomAge / 0.5) * 0.4 * globalA;
          const flashR = baseSize * bloomScale * 0.6;
          const brightR = Math.min(255, cr + 80);
          const brightG = Math.min(255, cg + 80);
          const brightB = Math.min(255, cb + 80);
          ctx.beginPath();
          ctx.arc(p.x, p.y, Math.max(1, flashR), 0, Math.PI * 2);
          ctx.fillStyle = `rgba(${brightR},${brightG},${brightB},${flashA.toFixed(3)})`;
          ctx.fill();
        }

        // Bloom sparks — emit 6-8 tiny motes during settle phase
        if (isBlooming && bloomAge > 0.2 && bloomAge < 1.2) {
          const sparkCount = 7;
          for (let si = 0; si < sparkCount; si++) {
            const sparkAge = bloomAge - 0.2;
            const sparkPhase = sparkAge / 1.0; // normalize to 0-1
            const angle = (si / sparkCount) * Math.PI * 2 + si * 0.7;
            const sparkDist = sparkPhase * (12 + si * 3) * scale;
            const sparkA = Math.max(0, (1 - sparkPhase) * 0.3 * globalA);
            const sparkSize = (0.3 + Math.random() * 0.1) * (1 - sparkPhase * 0.5);
            const sx = p.x + Math.cos(angle) * sparkDist;
            const sy = p.y + Math.sin(angle) * sparkDist;
            ctx.beginPath();
            ctx.arc(sx, sy, sparkSize, 0, Math.PI * 2);
            ctx.fillStyle = `rgba(${cr},${cg},${cb},${sparkA.toFixed(3)})`;
            ctx.fill();
          }
        }

        // Stardust orbiting
        if (hasStardust) {
          for (let d = 0; d < Math.min(star.stardust.length * 2, 8); d++) {
            const da = (d / 8) * Math.PI * 2 + t * 0.08;
            const dr = Math.max(5, baseSize * 3) + Math.sin(t * 0.3 + d) * 1.5;
            ctx.beginPath();
            ctx.arc(p.x + Math.cos(da) * dr, p.y + Math.sin(da) * dr, 0.35, 0, Math.PI * 2);
            ctx.fillStyle = `rgba(255,224,136,${(0.10 + Math.sin(t + d) * 0.03).toFixed(3)})`;
            ctx.fill();
          }
        }

        // Passage echo — MEMORY voice on canvas: user's own words, warm parchment
        if (star.passage && scale > 0.8 && !dimmed && !isBlooming) {
          const words = star.passage.split(/\s+/).slice(0, 4).join(' ');
          const echoA = (scale - 0.8) * 0.15 * twinkle * globalA;
          if (echoA > 0.01) {
            ctx.font = `300 ${Math.max(7, 8 * scale)}px ${SERIF}`;
            ctx.textAlign = 'center';
            ctx.fillStyle = `rgba(212,200,184,${echoA.toFixed(3)})`;
            ctx.fillText(words + '...', p.x, p.y - Math.max(12, baseSize * 3 + 6));
          }
        }
      } else {
        // Depth-of-field: distant unlit stars get softer, larger glow with lower peak alpha
        const dofSpread = scale < dofFar ? 1.6 : (scale < 0.6 ? 1.2 : 1.0);
        const alpha = (0.05 + Math.sin(t * 0.2 + star.pos.y * 0.008) * 0.02) * twinkle * globalA;
        const glowR = Math.max(2, baseSize * 2 * dofSpread);
        const grad = ctx.createRadialGradient(p.x, p.y, 0, p.x, p.y, glowR);
        grad.addColorStop(0, `rgba(${cr},${cg},${cb},${(alpha * 1.5 / dofSpread).toFixed(4)})`);
        grad.addColorStop(0.5, `rgba(${cr},${cg},${cb},${(alpha * 0.5 / dofSpread).toFixed(4)})`);
        grad.addColorStop(1, `rgba(${cr},${cg},${cb},0)`);
        ctx.fillStyle = grad;
        ctx.beginPath(); ctx.arc(p.x, p.y, glowR, 0, Math.PI * 2); ctx.fill();

        ctx.beginPath();
        ctx.arc(p.x, p.y, Math.max(0.6, baseSize * 0.6), 0, Math.PI * 2);
        ctx.fillStyle = `rgba(${cr},${cg},${cb},${(0.12 * twinkle * globalA + breathCycle * 0.02).toFixed(3)})`;
        ctx.fill();
      }

      // Star name — only focused or illuminated + close enough
      if (scale > 0.7 && baseSize > 1.2 && (isFocused || isIlluminated)) {
        ctx.font = `300 ${Math.max(8, 9 * scale)}px ${SANS}`;
        ctx.textAlign = 'center';
        ctx.fillStyle = `rgba(${cr},${cg},${cb},${(isFocused ? 0.35 : 0.12) * globalA})`;
        ctx.fillText(star.name, p.x, p.y + Math.max(12, baseSize * 2.5 + 8));
      }
    }

    // ── Clean up expired blooms ──
    bloomsRef.current = bloomsRef.current.filter(b => t - b.startTime < 2.5);

    // ── Orbit trails — motion blur during camera rotation ──
    const prevCam = prevCameraRef.current;
    const camDeltaYaw = camera.yaw - prevCam.yaw;
    const camDeltaPitch = camera.pitch - prevCam.pitch;
    const camSpeed = Math.sqrt(camDeltaYaw ** 2 + camDeltaPitch ** 2);
    if (camSpeed > 0.002 && !dimmed) {
      for (const p of projected) {
        if (p.behind || !p.star.illuminated) continue;
        const con = CONSTELLATIONS.find(c => c.starIds.includes(p.star.id));
        const [cr, cg, cb] = parseHex(con?.color || '#AAB0C0');
        const sx = p.x + camDeltaYaw * 600;
        const sy = p.y - camDeltaPitch * 600;
        const streakA = Math.min(0.06, camSpeed * 0.8) * globalA;
        const grad = ctx.createLinearGradient(sx, sy, p.x, p.y);
        grad.addColorStop(0, `rgba(${cr},${cg},${cb},0)`);
        grad.addColorStop(1, `rgba(${cr},${cg},${cb},${streakA.toFixed(4)})`);
        ctx.beginPath();
        ctx.moveTo(sx, sy);
        ctx.lineTo(p.x, p.y);
        ctx.strokeStyle = grad;
        ctx.lineWidth = 0.4;
        ctx.stroke();
      }
    }
    prevCameraRef.current = { yaw: camera.yaw, pitch: camera.pitch };

    // ── Constellation completion ceremony + territory completion evolution (Phase E Item 19) ──
    for (const constellation of CONSTELLATIONS) {
      const litCount = stars.filter(s => s.constellation === constellation.id && s.illuminated).length;
      const totalCount = constellation.starIds.length;
      if (litCount < totalCount) continue;

      const cp = project(constellation.center, camera, width, height);
      if (cp.behind || cp.scale < 0.15) continue;
      const [cr, cg, cb] = parseHex(constellation.color);
      const evo = computeTerritoryEvolution(constellation.id, stars);

      // Evolved completion halo — deeper breathing, wider rings
      const breathAmpEvo = evo.breathAmp;
      const glowScale = evo.glowMul;
      const ringPhase = (t * 0.3) % 1;
      for (let ring = 0; ring < 3; ring++) {
        const rp = (ringPhase + ring * 0.33) % 1;
        const ringR = (20 + ring * 8) * cp.scale + rp * (60 + ring * 20) * cp.scale;
        const ringA = (1 - rp) * (0.03 + breathAmpEvo * 0.5) * cp.scale * globalA / glowScale;
        ctx.beginPath();
        ctx.arc(cp.x, cp.y, ringR, 0, Math.PI * 2);
        ctx.strokeStyle = `rgba(${cr},${cg},${cb},${ringA.toFixed(4)})`;
        ctx.lineWidth = 0.3 + ring * 0.1;
        ctx.stroke();
      }

      // Evolved inner corona — warm glow that breathes at the constellation center
      const coronaBreath = Math.sin(t * 0.08) * breathAmpEvo + breathAmpEvo;
      const coronaR = (35 + coronaBreath * 200) * cp.scale;
      const coronaA = (0.008 + coronaBreath * 0.012) * cp.scale * globalA;
      const warmCr = Math.min(255, cr + 30);
      const warmCg = Math.min(255, cg + 20);
      const warmCb = Math.min(255, cb + 10);
      const coronaGrad = ctx.createRadialGradient(cp.x, cp.y, 0, cp.x, cp.y, coronaR);
      coronaGrad.addColorStop(0, `rgba(${warmCr},${warmCg},${warmCb},${coronaA.toFixed(4)})`);
      coronaGrad.addColorStop(0.5, `rgba(${cr},${cg},${cb},${(coronaA * 0.4).toFixed(4)})`);
      coronaGrad.addColorStop(1, `rgba(${cr},${cg},${cb},0)`);
      ctx.fillStyle = coronaGrad;
      ctx.beginPath(); ctx.arc(cp.x, cp.y, coronaR, 0, Math.PI * 2); ctx.fill();

      // Pulsing energy along threads — evolved with richer particles
      for (const [idA, idB] of constellation.connections) {
        const a = projected.find(p => p.id === idA);
        const b = projected.find(p => p.id === idB);
        if (!a || !b || a.behind || b.behind) continue;

        // Primary pulse
        const pulsePos = ((t * 0.15) % 1);
        const px = a.x + (b.x - a.x) * pulsePos;
        const py = a.y + (b.y - a.y) * pulsePos;
        const pulseA = 0.10 * (1 - Math.abs(pulsePos - 0.5) * 2) * cp.scale * globalA;

        ctx.beginPath();
        ctx.arc(px, py, 1.5, 0, Math.PI * 2);
        ctx.fillStyle = `rgba(${cr},${cg},${cb},${pulseA.toFixed(3)})`;
        ctx.fill();

        // Secondary counter-pulse (evolved territories get bidirectional flow)
        const pulse2Pos = ((t * 0.12 + 0.5) % 1);
        const p2x = b.x + (a.x - b.x) * pulse2Pos;
        const p2y = b.y + (a.y - b.y) * pulse2Pos;
        const pulse2A = 0.06 * (1 - Math.abs(pulse2Pos - 0.5) * 2) * cp.scale * globalA;
        ctx.beginPath();
        ctx.arc(p2x, p2y, 1, 0, Math.PI * 2);
        ctx.fillStyle = `rgba(${warmCr},${warmCg},${warmCb},${pulse2A.toFixed(3)})`;
        ctx.fill();
      }
    }

    // ── Cross-constellation bridges — energy arcs between completed constellations ──
    const completedCons = CONSTELLATIONS.filter(c =>
      c.starIds.every(sid => stars.find(s => s.id === sid)?.illuminated)
    );
    if (completedCons.length >= 2 && !dimmed) {
      for (let i = 0; i < completedCons.length; i++) {
        for (let j = i + 1; j < completedCons.length; j++) {
          const a = completedCons[i];
          const b = completedCons[j];
          const pa = project(a.center, camera, width, height);
          const pb = project(b.center, camera, width, height);
          if (pa.behind || pb.behind) continue;

          const [ar, ag, ab] = parseHex(a.color);
          const [br, bg, bb] = parseHex(b.color);

          // Arc — subtle bezier curve between centers
          const mx = (pa.x + pb.x) / 2;
          const my = (pa.y + pb.y) / 2 - 30 * Math.min(pa.scale, pb.scale);
          const arcA = 0.015 * Math.min(pa.scale, pb.scale) * globalA;

          ctx.beginPath();
          ctx.moveTo(pa.x, pa.y);
          ctx.quadraticCurveTo(mx, my, pb.x, pb.y);
          const arcGrad = ctx.createLinearGradient(pa.x, pa.y, pb.x, pb.y);
          arcGrad.addColorStop(0, `rgba(${ar},${ag},${ab},${arcA.toFixed(4)})`);
          arcGrad.addColorStop(0.5, `rgba(${Math.round((ar+br)/2)},${Math.round((ag+bg)/2)},${Math.round((ab+bb)/2)},${(arcA * 0.6).toFixed(4)})`);
          arcGrad.addColorStop(1, `rgba(${br},${bg},${bb},${arcA.toFixed(4)})`);
          ctx.strokeStyle = arcGrad;
          ctx.lineWidth = 0.3;
          ctx.stroke();

          // Energy mote traveling along the arc
          const motePhase = ((t * 0.1 + i * 0.3 + j * 0.17) % 1);
          const mt = motePhase;
          const moteX = (1-mt)*(1-mt)*pa.x + 2*(1-mt)*mt*mx + mt*mt*pb.x;
          const moteY = (1-mt)*(1-mt)*pa.y + 2*(1-mt)*mt*my + mt*mt*pb.y;
          const moteA = 0.12 * (1 - Math.abs(mt - 0.5) * 2) * Math.min(pa.scale, pb.scale) * globalA;
          const moteR = Math.round(ar + (br - ar) * mt);
          const moteG = Math.round(ag + (bg - ag) * mt);
          const moteB = Math.round(ab + (bb - ab) * mt);

          ctx.beginPath();
          ctx.arc(moteX, moteY, 1.5, 0, Math.PI * 2);
          ctx.fillStyle = `rgba(${moteR},${moteG},${moteB},${moteA.toFixed(3)})`;
          ctx.fill();

          // Second mote offset by 0.5 phase
          const mt2 = (mt + 0.5) % 1;
          const mote2X = (1-mt2)*(1-mt2)*pa.x + 2*(1-mt2)*mt2*mx + mt2*mt2*pb.x;
          const mote2Y = (1-mt2)*(1-mt2)*pa.y + 2*(1-mt2)*mt2*my + mt2*mt2*pb.y;
          const mote2A = 0.08 * (1 - Math.abs(mt2 - 0.5) * 2) * Math.min(pa.scale, pb.scale) * globalA;

          ctx.beginPath();
          ctx.arc(mote2X, mote2Y, 1, 0, Math.PI * 2);
          ctx.fillStyle = `rgba(${Math.round(ar + (br - ar) * mt2)},${Math.round(ag + (bg - ag) * mt2)},${Math.round(ab + (bb - ab) * mt2)},${mote2A.toFixed(3)})`;
          ctx.fill();
        }
      }
    }

    // ── Phase E Item 20: Cross-territory thematic resonance arcs ──
    // Detect thematic overlaps and render luminous resonance threads between territories
    if (!dimmed) {
      const resonances = detectResonances(stars);
      for (const res of resonances.slice(0, 3)) { // cap at 3 strongest
        if (res.resonatingTerritories.length < 2) continue;
        // Draw resonance arcs between each pair of resonating territories
        for (let ri = 0; ri < res.resonatingTerritories.length - 1; ri++) {
          const tA = res.resonatingTerritories[ri];
          const tB = res.resonatingTerritories[ri + 1];
          const conA = CONSTELLATIONS.find(cc => cc.schema === tA);
          const conB = CONSTELLATIONS.find(cc => cc.schema === tB);
          if (!conA || !conB) continue;

          const pa = project(conA.center, camera, width, height);
          const pb = project(conB.center, camera, width, height);
          if (pa.behind || pb.behind) continue;

          const [arR, arG, arB] = parseHex(conA.color);
          const [brR, brG, brB] = parseHex(conB.color);

          // Resonance strength controls visibility
          const resAlpha = Math.min(0.025, 0.005 * res.strength) * Math.min(pa.scale, pb.scale) * globalA;
          if (resAlpha < 0.001) continue;

          // Resonance arc — wider, more organic than bridge arcs
          const resMx = (pa.x + pb.x) / 2 + Math.sin(t * 0.05 + ri) * 20;
          const resMy = (pa.y + pb.y) / 2 - 50 * Math.min(pa.scale, pb.scale) + Math.cos(t * 0.04) * 15;

          ctx.beginPath();
          ctx.moveTo(pa.x, pa.y);
          ctx.quadraticCurveTo(resMx, resMy, pb.x, pb.y);
          const resGrad = ctx.createLinearGradient(pa.x, pa.y, pb.x, pb.y);
          resGrad.addColorStop(0, `rgba(${arR},${arG},${arB},${resAlpha.toFixed(4)})`);
          // Blended midpoint — the resonance color
          const midR = Math.round((arR + brR) / 2);
          const midG = Math.round((arG + brG) / 2);
          const midB = Math.round((arB + brB) / 2);
          resGrad.addColorStop(0.5, `rgba(${midR},${midG},${midB},${(resAlpha * 1.3).toFixed(4)})`);
          resGrad.addColorStop(1, `rgba(${brR},${brG},${brB},${resAlpha.toFixed(4)})`);
          ctx.strokeStyle = resGrad;
          ctx.lineWidth = 0.4;
          ctx.setLineDash([3, 6]);
          ctx.stroke();
          ctx.setLineDash([]);

          // Resonance shimmer mote — slower, more organic than bridge motes
          const shimmerPhase = ((t * 0.06 + ri * 0.4) % 1);
          const st = shimmerPhase;
          const smX = (1 - st) * (1 - st) * pa.x + 2 * (1 - st) * st * resMx + st * st * pb.x;
          const smY = (1 - st) * (1 - st) * pa.y + 2 * (1 - st) * st * resMy + st * st * pb.y;
          const smA = 0.15 * (1 - Math.abs(st - 0.5) * 2) * Math.min(pa.scale, pb.scale) * globalA;

          ctx.beginPath();
          ctx.arc(smX, smY, 2, 0, Math.PI * 2);
          const smGlow = ctx.createRadialGradient(smX, smY, 0, smX, smY, 6);
          smGlow.addColorStop(0, `rgba(${midR},${midG},${midB},${smA.toFixed(3)})`);
          smGlow.addColorStop(1, `rgba(${midR},${midG},${midB},0)`);
          ctx.fillStyle = smGlow;
          ctx.fill();
        }
      }
    }

    // ── Phase G Item 27: Session memory threads ──
    // Golden luminous trails connecting stars illuminated in the current session
    if (sessionStarIds.length >= 2 && !dimmed) {
      const sessionProjected = sessionStarIds
        .map(sid => projected.find(p => p.id === sid))
        .filter((p): p is NonNullable<typeof p> => !!p && !p.behind);

      if (sessionProjected.length >= 2) {
        for (let i = 0; i < sessionProjected.length - 1; i++) {
          const a = sessionProjected[i];
          const b = sessionProjected[i + 1];

          // Golden memory thread — organic bezier
          const memMx = (a.x + b.x) / 2 + Math.sin(t * 0.03 + i * 1.7) * 15;
          const memMy = (a.y + b.y) / 2 - 20 * Math.min(a.scale, b.scale) + Math.cos(t * 0.04 + i * 2.1) * 10;
          const memAlpha = 0.018 * Math.min(a.scale, b.scale) * globalA;

          if (memAlpha > 0.001) {
            ctx.beginPath();
            ctx.moveTo(a.x, a.y);
            ctx.quadraticCurveTo(memMx, memMy, b.x, b.y);

            const memGrad = ctx.createLinearGradient(a.x, a.y, b.x, b.y);
            memGrad.addColorStop(0, `rgba(255,224,136,${memAlpha.toFixed(4)})`);
            memGrad.addColorStop(0.5, `rgba(255,210,120,${(memAlpha * 1.3).toFixed(4)})`);
            memGrad.addColorStop(1, `rgba(255,224,136,${memAlpha.toFixed(4)})`);
            ctx.strokeStyle = memGrad;
            ctx.lineWidth = 0.35;
            ctx.setLineDash([4, 8]);
            ctx.stroke();
            ctx.setLineDash([]);

            // Memory mote — golden spark traveling along the thread
            const memPhase = ((t * 0.08 + i * 0.4) % 1);
            const mmt = memPhase;
            const mmX = (1 - mmt) * (1 - mmt) * a.x + 2 * (1 - mmt) * mmt * memMx + mmt * mmt * b.x;
            const mmY = (1 - mmt) * (1 - mmt) * a.y + 2 * (1 - mmt) * mmt * memMy + mmt * mmt * b.y;
            const mmA = 0.2 * (1 - Math.abs(mmt - 0.5) * 2) * Math.min(a.scale, b.scale) * globalA;

            ctx.beginPath();
            ctx.arc(mmX, mmY, 1.2, 0, Math.PI * 2);
            const mmGlow = ctx.createRadialGradient(mmX, mmY, 0, mmX, mmY, 4);
            mmGlow.addColorStop(0, `rgba(255,224,136,${mmA.toFixed(3)})`);
            mmGlow.addColorStop(1, 'rgba(255,224,136,0)');
            ctx.fillStyle = mmGlow;
            ctx.fill();
          }
        }
      }
    }

    // ── Phase G Item 28: Territory breath variation ──
    // Each constellation's atmosphere breathes at its own rhythm
    if (territoryBreaths.length > 0 && !dimmed) {
      for (const tb of territoryBreaths) {
        const con = CONSTELLATIONS.find(c => c.id === tb.conId);
        if (!con) continue;
        const litCount = stars.filter(s => s.constellation === con.id && s.illuminated).length;
        if (litCount === 0) continue;

        const cp = project(con.center, camera, width, height);
        if (cp.behind || cp.scale < 0.2) continue;

        const [cr, cg, cb] = parseHex(con.color);
        const breathVal = Math.sin(t * tb.frequency * Math.PI * 2) * tb.amplitude;
        const breathAbs = Math.abs(breathVal);

        // Breath-modulated atmosphere ring — unique per territory
        const ringR = (40 + breathAbs * 25) * cp.scale;
        const ringA = (0.003 + breathAbs * 0.006) * cp.scale * globalA * tb.warmthBoost;

        if (ringA > 0.001) {
          const breathGrad = ctx.createRadialGradient(cp.x, cp.y, ringR * 0.3, cp.x, cp.y, ringR);
          breathGrad.addColorStop(0, `rgba(${cr},${cg},${cb},${ringA.toFixed(4)})`);
          breathGrad.addColorStop(0.6, `rgba(${cr},${cg},${cb},${(ringA * 0.4).toFixed(4)})`);
          breathGrad.addColorStop(1, `rgba(${cr},${cg},${cb},0)`);
          ctx.fillStyle = breathGrad;
          ctx.beginPath(); ctx.arc(cp.x, cp.y, ringR, 0, Math.PI * 2); ctx.fill();
        }

        // Beckoning pulse — for territories that have unlocked depth but are underexplored
        if (tb.beckoning) {
          const beckonPhase = ((t * 0.15) % 1);
          const beckonR = (20 + beckonPhase * 50) * cp.scale;
          const beckonA = (1 - beckonPhase) * 0.012 * cp.scale * globalA;
          ctx.beginPath();
          ctx.arc(cp.x, cp.y, beckonR, 0, Math.PI * 2);
          ctx.strokeStyle = `rgba(${cr},${cg},${cb},${beckonA.toFixed(4)})`;
          ctx.lineWidth = 0.25;
          ctx.stroke();
        }
      }
    }

    // ── Phase I Item 33: Inscription ripple — visual shockwave when a star illuminates ──
    if (inscriptionRipples.length > 0 && !dimmed) {
      for (const ripple of inscriptionRipples) {
        const age = t - ripple.birth;
        if (age < 0 || age > ripple.duration) continue;
        
        const progress = age / ripple.duration;
        const star = stars.find(s => s.id === ripple.starId);
        if (!star) continue;
        
        const p = project(star.pos, camera, width, height);
        if (p.behind) continue;
        
        const [rr, rg, rb] = parseHex(ripple.color);
        const currentRadius = progress * ripple.maxRadius * p.scale;
        
        // Ripple fades out and thins as it expands
        const fadeIn = Math.min(1, progress * 8);
        const fadeOut = 1 - Math.pow(progress, 0.4);
        const rippleAlpha = fadeIn * fadeOut * 0.08 * globalA;
        
        if (rippleAlpha > 0.001 && currentRadius > 2) {
          // Primary ripple ring
          ctx.beginPath();
          ctx.arc(p.x, p.y, currentRadius, 0, Math.PI * 2);
          ctx.strokeStyle = `rgba(${rr},${rg},${rb},${rippleAlpha.toFixed(4)})`;
          ctx.lineWidth = Math.max(0.3, (1 - progress) * 1.5);
          ctx.stroke();
          
          // Secondary inner ring — slightly behind
          if (progress > 0.1) {
            const innerProgress = Math.max(0, progress - 0.1) / 0.9;
            const innerRadius = innerProgress * ripple.maxRadius * p.scale * 0.7;
            const innerAlpha = rippleAlpha * 0.5 * (1 - innerProgress);
            if (innerAlpha > 0.001) {
              ctx.beginPath();
              ctx.arc(p.x, p.y, innerRadius, 0, Math.PI * 2);
              ctx.strokeStyle = `rgba(${rr},${rg},${rb},${innerAlpha.toFixed(4)})`;
              ctx.lineWidth = Math.max(0.2, (1 - innerProgress) * 0.8);
              ctx.stroke();
            }
          }
          
          // Soft glow wash behind the ripple
          const washRadius = currentRadius * 0.6;
          const washAlpha = rippleAlpha * 0.4;
          const washGrad = ctx.createRadialGradient(p.x, p.y, 0, p.x, p.y, washRadius);
          washGrad.addColorStop(0, `rgba(${rr},${rg},${rb},${washAlpha.toFixed(4)})`);
          washGrad.addColorStop(0.5, `rgba(${rr},${rg},${rb},${(washAlpha * 0.3).toFixed(4)})`);
          washGrad.addColorStop(1, `rgba(${rr},${rg},${rb},0)`);
          ctx.fillStyle = washGrad;
          ctx.beginPath(); ctx.arc(p.x, p.y, washRadius, 0, Math.PI * 2); ctx.fill();
        }
      }
    }

    // ── Phase I Item 35: Star proximity micro-constellations ──
    // When zoomed close (scale > 1.0), illuminated stars reveal private micro-constellations
    if (!dimmed) {
      for (const p of projected) {
        if (p.behind || p.scale < 1.0 || !p.star.illuminated || p.star.stardust.length < 2) continue;
        
        const constellation = CONSTELLATIONS.find(c => c.starIds.includes(p.star.id));
        const color = constellation?.color || '#AAB0C0';
        const [mcr, mcg, mcb] = parseHex(color);
        
        // Micro-constellation visibility scales with proximity
        const microAlpha = Math.min(0.25, (p.scale - 1.0) * 0.3) * globalA;
        if (microAlpha < 0.01) continue;
        
        const count = Math.min(p.star.stardust.length * 2, 8);
        const goldenAngle = Math.PI * (3 - Math.sqrt(5));
        const microNodes: { x: number; y: number }[] = [];
        
        for (let mi = 0; mi < count; mi++) {
          const theta = goldenAngle * mi + (p.star.id.charCodeAt(0) || 0) * 0.1;
          const r = (8 + (mi / count) * 14 + (mi % 2) * 4) * p.scale * 0.5;
          const drift = Math.sin(t * 0.2 + mi * 1.7) * 1.2;
          const nx = p.x + Math.cos(theta) * r + drift;
          const ny = p.y + Math.sin(theta) * r + Math.cos(t * 0.15 + mi * 2.3) * 0.8;
          microNodes.push({ x: nx, y: ny });
          
          // Micro-star point
          const nodeAlpha = microAlpha * (0.6 + (1 - mi / count) * 0.4);
          const nodeSize = (0.4 + Math.random() * 0.3) * p.scale * 0.4;
          ctx.beginPath();
          ctx.arc(nx, ny, Math.max(0.3, nodeSize), 0, Math.PI * 2);
          ctx.fillStyle = `rgba(${mcr},${mcg},${mcb},${nodeAlpha.toFixed(3)})`;
          ctx.fill();
        }
        
        // Micro-constellation lines
        for (let mi = 0; mi < microNodes.length - 1; mi++) {
          const a = microNodes[mi];
          const b = microNodes[mi + 1];
          const lineAlpha = microAlpha * 0.4;
          ctx.beginPath();
          ctx.moveTo(a.x, a.y);
          ctx.lineTo(b.x, b.y);
          ctx.strokeStyle = `rgba(${mcr},${mcg},${mcb},${lineAlpha.toFixed(4)})`;
          ctx.lineWidth = 0.2;
          ctx.stroke();
        }
        // Cross-link
        if (microNodes.length >= 4) {
          const a = microNodes[0];
          const b = microNodes[Math.floor(microNodes.length / 2)];
          ctx.beginPath();
          ctx.moveTo(a.x, a.y);
          ctx.lineTo(b.x, b.y);
          ctx.strokeStyle = `rgba(${mcr},${mcg},${mcb},${(microAlpha * 0.25).toFixed(4)})`;
          ctx.lineWidth = 0.15;
          ctx.stroke();
        }
      }
    }

    // ── Phase I Item 36: Passage tide — sequential breathing wave ──
    if (passageTides.length > 0 && !dimmed) {
      for (const tide of passageTides) {
        const con = CONSTELLATIONS.find(c => c.id === tide.conId);
        if (!con) continue;
        const [tcr, tcg, tcb] = parseHex(con.color);
        
        const wavePhase = (t * tide.speed) % 1;
        
        for (let si = 0; si < tide.sequence.length; si++) {
          const starId = tide.sequence[si];
          const p = projected.find(pp => pp.id === starId);
          if (!p || p.behind || p.scale < 0.3) continue;
          
          // Each star has a phase offset based on its position in the sequence
          const starPhase = si / tide.sequence.length;
          const waveDist = Math.abs(((wavePhase - starPhase) + 1.5) % 1 - 0.5) * 2; // 0 = wave is here, 1 = wave is far
          const waveIntensity = Math.max(0, 1 - waveDist * 2.5); // sharp peak
          
          if (waveIntensity > 0.01) {
            // Tide glow — warm pulse that flows through the sequence
            const tideR = Math.max(3, 5 * p.scale * waveIntensity);
            const tideAlpha = waveIntensity * 0.06 * p.scale * globalA;
            const tideGrad = ctx.createRadialGradient(p.x, p.y, 0, p.x, p.y, tideR);
            const warmTcr = Math.min(255, tcr + 20);
            const warmTcg = Math.min(255, tcg + 15);
            tideGrad.addColorStop(0, `rgba(${warmTcr},${warmTcg},${tcb},${tideAlpha.toFixed(4)})`);
            tideGrad.addColorStop(0.5, `rgba(${tcr},${tcg},${tcb},${(tideAlpha * 0.4).toFixed(4)})`);
            tideGrad.addColorStop(1, `rgba(${tcr},${tcg},${tcb},0)`);
            ctx.fillStyle = tideGrad;
            ctx.beginPath(); ctx.arc(p.x, p.y, tideR, 0, Math.PI * 2); ctx.fill();
          }
        }
      }
    }

    // ── Phase J Item 37: Stardust memory shimmer ──
    // When zoomed close, stardust texts shimmer briefly near their stars
    if (stardustMemories.length > 0 && !dimmed) {
      for (const mem of stardustMemories) {
        if (mem.visibility < 0.05) continue;
        const star = stars.find(s => s.id === mem.starId);
        if (!star) continue;
        const p = project(star.pos, camera, width, height);
        if (p.behind || p.scale < 0.6) continue;

        const [mr, mg, mb] = parseHex(mem.color);
        const shimmerT = t * 0.15;

        for (let ti = 0; ti < Math.min(mem.texts.length, 3); ti++) {
          const text = mem.texts[ti];
          // Extract a short fragment (3-5 words)
          const words = text.split(/\s+/).slice(0, 4).join(' ');
          // Position orbiting around the star at varying radii
          const angle = shimmerT + ti * (Math.PI * 2 / Math.min(mem.texts.length, 3));
          const radius = (18 + ti * 12) * p.scale;
          const sx = p.x + Math.cos(angle) * radius;
          const sy = p.y + Math.sin(angle) * radius * 0.7;
          // Shimmer: fade in and out with different phases
          const shimmerPhase = Math.sin(shimmerT * 2 + ti * 2.1);
          const shimmerAlpha = Math.max(0, shimmerPhase * 0.35 + 0.05) * mem.visibility * globalA;

          if (shimmerAlpha > 0.01) {
            ctx.font = `300 ${Math.max(7, 7.5 * p.scale)}px ${SERIF}`;
            ctx.textAlign = 'center';
            ctx.fillStyle = `rgba(${mr},${mg},${mb},${shimmerAlpha.toFixed(4)})`;
            ctx.fillText(words, sx, sy);
          }
        }
      }
    }

    // ── Phase J Item 38: Territory harmonic resonance ──
    // Completed constellations synchronize their breathing when both visible
    if (harmonicPairs.length > 0 && !dimmed) {
      for (const pair of harmonicPairs) {
        const conA = CONSTELLATIONS.find(c => c.id === pair.conIdA);
        const conB = CONSTELLATIONS.find(c => c.id === pair.conIdB);
        if (!conA || !conB) continue;

        const pa = project(conA.center, camera, width, height);
        const pb = project(conB.center, camera, width, height);
        if (pa.behind || pb.behind || pa.scale < 0.2 || pb.scale < 0.2) continue;

        const [arh, agh, abh] = parseHex(pair.colorA);
        const [brh, bgh, bbh] = parseHex(pair.colorB);

        // Synchronized breathing pulse — blends between individual and shared frequency
        const sharedBreath = Math.sin(t * pair.sharedFrequency * Math.PI * 2);
        const syncPulse = sharedBreath * pair.coherence;

        // Harmonic resonance line — a luminous thread that pulses with coherence
        const hmx = (pa.x + pb.x) / 2 + Math.sin(t * 0.03 + pair.coherence) * 25;
        const hmy = (pa.y + pb.y) / 2 - 35 * Math.min(pa.scale, pb.scale) + Math.cos(t * 0.025) * 15;
        const harmAlpha = pair.coherence * 0.02 * Math.min(pa.scale, pb.scale) * globalA * (0.7 + syncPulse * 0.3);

        if (harmAlpha > 0.001) {
          ctx.beginPath();
          ctx.moveTo(pa.x, pa.y);
          ctx.quadraticCurveTo(hmx, hmy, pb.x, pb.y);
          const harmGrad = ctx.createLinearGradient(pa.x, pa.y, pb.x, pb.y);
          // Blended color at midpoint
          const midHr = Math.round((arh + brh) / 2);
          const midHg = Math.round((agh + bgh) / 2);
          const midHb = Math.round((abh + bbh) / 2);
          harmGrad.addColorStop(0, `rgba(${arh},${agh},${abh},${harmAlpha.toFixed(4)})`);
          harmGrad.addColorStop(0.5, `rgba(${midHr},${midHg},${midHb},${(harmAlpha * 1.5).toFixed(4)})`);
          harmGrad.addColorStop(1, `rgba(${brh},${bgh},${bbh},${harmAlpha.toFixed(4)})`);
          ctx.strokeStyle = harmGrad;
          ctx.lineWidth = 0.5 + pair.coherence * 0.3;
          ctx.stroke();

          // Synchronized glow at each constellation center
          const syncGlowR = (12 + syncPulse * 8) * pa.scale * pair.coherence;
          const syncGlowA = 0.008 * pair.coherence * globalA * (0.8 + syncPulse * 0.2);
          if (syncGlowA > 0.001 && syncGlowR > 2) {
            // Glow at A
            const sgA = ctx.createRadialGradient(pa.x, pa.y, 0, pa.x, pa.y, syncGlowR);
            sgA.addColorStop(0, `rgba(${midHr},${midHg},${midHb},${syncGlowA.toFixed(4)})`);
            sgA.addColorStop(1, `rgba(${midHr},${midHg},${midHb},0)`);
            ctx.fillStyle = sgA;
            ctx.beginPath(); ctx.arc(pa.x, pa.y, syncGlowR, 0, Math.PI * 2); ctx.fill();
            // Glow at B
            const syncGlowRB = (12 + syncPulse * 8) * pb.scale * pair.coherence;
            const sgB = ctx.createRadialGradient(pb.x, pb.y, 0, pb.x, pb.y, syncGlowRB);
            sgB.addColorStop(0, `rgba(${midHr},${midHg},${midHb},${syncGlowA.toFixed(4)})`);
            sgB.addColorStop(1, `rgba(${midHr},${midHg},${midHb},0)`);
            ctx.fillStyle = sgB;
            ctx.beginPath(); ctx.arc(pb.x, pb.y, syncGlowRB, 0, Math.PI * 2); ctx.fill();
          }
        }
      }
    }

    // ── Phase K Item 41: Passage weaving threads ──
    // Visual threads connecting stars whose passages share language
    if (passageWeaves.length > 0 && !dimmed) {
      for (const weave of passageWeaves) {
        const starA = stars.find(s => s.id === weave.starIdA);
        const starB = stars.find(s => s.id === weave.starIdB);
        if (!starA || !starB) continue;

        const pa = project(starA.pos, camera, width, height);
        const pb = project(starB.pos, camera, width, height);
        if (pa.behind || pb.behind || pa.scale < 0.25 || pb.scale < 0.25) continue;

        const [arw, agw, abw] = parseHex(weave.colorA);
        const [brw, bgw, bbw] = parseHex(weave.colorB);

        // Weave thread — a delicate dashed arc between the two stars
        const wmx = (pa.x + pb.x) / 2 + Math.sin(t * 0.04 + weave.strength) * 30;
        const wmy = (pa.y + pb.y) / 2 - 40 * Math.min(pa.scale, pb.scale) + Math.cos(t * 0.03) * 20;
        const weaveAlpha = weave.strength * 0.018 * Math.min(pa.scale, pb.scale) * globalA;

        if (weaveAlpha > 0.001) {
          ctx.beginPath();
          ctx.moveTo(pa.x, pa.y);
          ctx.quadraticCurveTo(wmx, wmy, pb.x, pb.y);
          const wGrad = ctx.createLinearGradient(pa.x, pa.y, pb.x, pb.y);
          wGrad.addColorStop(0, `rgba(${arw},${agw},${abw},${weaveAlpha.toFixed(4)})`);
          const wMidR = Math.round((arw + brw) / 2);
          const wMidG = Math.round((agw + bgw) / 2);
          const wMidB = Math.round((abw + bbw) / 2);
          wGrad.addColorStop(0.5, `rgba(${wMidR},${wMidG},${wMidB},${(weaveAlpha * 1.4).toFixed(4)})`);
          wGrad.addColorStop(1, `rgba(${brw},${bgw},${bbw},${weaveAlpha.toFixed(4)})`);
          ctx.strokeStyle = wGrad;
          ctx.lineWidth = 0.35;
          ctx.setLineDash([2, 5]);
          ctx.stroke();
          ctx.setLineDash([]);

          // Weave mote — golden shimmer traveling the thread
          const wPhase = ((t * 0.07 + weave.strength * 2) % 1);
          const wt = wPhase;
          const wmX = (1-wt)*(1-wt)*pa.x + 2*(1-wt)*wt*wmx + wt*wt*pb.x;
          const wmY = (1-wt)*(1-wt)*pa.y + 2*(1-wt)*wt*wmy + wt*wt*pb.y;
          const wmA = 0.18 * (1 - Math.abs(wt - 0.5) * 2) * Math.min(pa.scale, pb.scale) * globalA * weave.strength;

          ctx.beginPath();
          ctx.arc(wmX, wmY, 1.5, 0, Math.PI * 2);
          const wmGlow = ctx.createRadialGradient(wmX, wmY, 0, wmX, wmY, 5);
          wmGlow.addColorStop(0, `rgba(${wMidR},${wMidG},${wMidB},${wmA.toFixed(3)})`);
          wmGlow.addColorStop(1, `rgba(${wMidR},${wMidG},${wMidB},0)`);
          ctx.fillStyle = wmGlow;
          ctx.fill();
        }
      }
    }

    // ── Phase L+: Weave glow words — staggered fade sequence ──
    // Words appear one by one with 400ms delay between each,
    // fading in over 1.2s with an elastic ease before settling into their orbit
    if (weaveGlowWords.length > 0 && !dimmed) {
      ctx.save();
      ctx.textAlign = 'center';
      ctx.textBaseline = 'middle';
      const nowMs = performance.now();
      const elapsedSinceStart = weaveGlowStartTime > 0 ? nowMs - weaveGlowStartTime : 99999;
      // Adaptive stagger: total reveal takes ~3s regardless of word count
      // Fewer words get longer individual delays, more words get tighter sequence
      const TOTAL_REVEAL_MS = 3000;
      const wordCount = weaveGlowWords.length;
      const STAGGER_DELAY = wordCount > 1
        ? Math.min(600, Math.max(120, TOTAL_REVEAL_MS / (wordCount + 2)))
        : 400;
      const FADE_DURATION = Math.max(800, 1200 - wordCount * 30); // slightly faster for large sets

      for (let wi = 0; wi < weaveGlowWords.length; wi++) {
        const gw = weaveGlowWords[wi];
        const star = stars.find(s => s.id === gw.starId);
        if (!star) continue;
        const sp = project(star.pos, camera, width, height);
        if (sp.behind || sp.scale < 0.3) continue;

        // Stagger: this word begins fading in after wi * STAGGER_DELAY ms
        const wordStartMs = wi * STAGGER_DELAY;
        const wordElapsed = elapsedSinceStart - wordStartMs;
        if (wordElapsed <= 0) continue; // not yet revealed

        // Fade progress 0..1 with cubic ease-out
        const fadeRaw = Math.min(1, wordElapsed / FADE_DURATION);
        const fadeEased = 1 - Math.pow(1 - fadeRaw, 3);

        const [gr, gg, gb] = parseHex(gw.conColor);
        // Each word orbits its star at a unique angle based on its index and passage position
        const angle = gw.positionInPassage * Math.PI * 2 + wi * 1.1 + t * 0.015;
        // Entrance: words drift outward from star center as they appear
        const baseRadius = 18 + gw.positionInPassage * 12;
        const radius = baseRadius * (0.3 + 0.7 * fadeEased);
        const wx = sp.x + Math.cos(angle) * radius * sp.scale;
        const wy = sp.y + Math.sin(angle) * radius * sp.scale;

        // Pulsing alpha — each word breathes at its own rate, modulated by fade
        const pulse = Math.sin(t * 0.06 + wi * 0.7) * 0.5 + 0.5;
        const wordAlpha = 0.25 * pulse * sp.scale * globalA * fadeEased;

        if (wordAlpha > 0.01) {
          // Glow halo behind word — larger during entrance, settles down
          const entranceGlow = 1 + (1 - fadeEased) * 1.5;
          const glowR = 8 * sp.scale * entranceGlow;
          const wordGlow = ctx.createRadialGradient(wx, wy, 0, wx, wy, glowR);
          wordGlow.addColorStop(0, `rgba(${gr},${gg},${gb},${(wordAlpha * 0.6).toFixed(3)})`);
          wordGlow.addColorStop(1, `rgba(${gr},${gg},${gb},0)`);
          ctx.beginPath();
          ctx.arc(wx, wy, glowR, 0, Math.PI * 2);
          ctx.fillStyle = wordGlow;
          ctx.fill();

          // Word text
          const fontSize = Math.max(7, Math.min(11, 9 * sp.scale));
          ctx.font = `300 ${fontSize}px -apple-system, BlinkMacSystemFont, sans-serif`;
          ctx.fillStyle = `rgba(${gr},${gg},${gb},${(wordAlpha * 1.5).toFixed(3)})`;
          ctx.fillText(gw.word, wx, wy);
        }
      }
      ctx.restore();
    }

    // ── Phase J Item 40: Constellation gravity wells ──
    // Stars with deep passages pull nearby dust particles, creating visual mass
    if (gravityWells.length > 0 && !dimmed) {
      for (const well of gravityWells) {
        const wp = project(well.pos, camera, width, height);
        if (wp.behind || wp.scale < 0.3) continue;

        const [wr, wg, wb] = parseHex(well.color);
        const wellScreenRadius = well.radius * wp.scale;

        // Gravity distortion — subtle concentric rings that compress toward center
        const ringCount = Math.min(3, 1 + Math.floor(well.strength * 3));
        for (let ri = 0; ri < ringCount; ri++) {
          const ringProgress = (ri + 1) / (ringCount + 1);
          const ringR = wellScreenRadius * ringProgress;
          const ringBreath = Math.sin(t * 0.06 + ri * 1.5 + well.pos.x * 0.01) * 0.3 + 0.7;
          const ringA = well.strength * 0.012 * (1 - ringProgress) * wp.scale * globalA * ringBreath;

          if (ringA > 0.001 && ringR > 2) {
            ctx.beginPath();
            ctx.arc(wp.x, wp.y, ringR, 0, Math.PI * 2);
            ctx.strokeStyle = `rgba(${wr},${wg},${wb},${ringA.toFixed(4)})`;
            ctx.lineWidth = 0.15 + well.strength * 0.1;
            ctx.stroke();
          }
        }

        // Attracted motes — tiny particles that drift inward
        const moteCount = Math.min(5, 2 + Math.floor(well.strength * 4));
        for (let mi = 0; mi < moteCount; mi++) {
          const moteAngle = t * (0.05 + mi * 0.02) + mi * (Math.PI * 2 / moteCount) + well.pos.y * 0.01;
          const moteDist = wellScreenRadius * (0.3 + ((Math.sin(t * 0.08 + mi * 3.7) + 1) / 2) * 0.7);
          const moteX = wp.x + Math.cos(moteAngle) * moteDist;
          const moteY = wp.y + Math.sin(moteAngle) * moteDist;
          const moteSize = (0.3 + well.strength * 0.3) * wp.scale;
          const moteAlpha = well.strength * 0.15 * (1 - moteDist / wellScreenRadius) * globalA;

          if (moteAlpha > 0.005) {
            ctx.beginPath();
            ctx.arc(moteX, moteY, Math.max(0.2, moteSize), 0, Math.PI * 2);
            ctx.fillStyle = `rgba(${wr},${wg},${wb},${moteAlpha.toFixed(4)})`;
            ctx.fill();
          }
        }
      }
    }

    // ── Constellation completion particle burst ──
    // Spawn particles when a constellation is newly completed
    for (const con of completedCons) {
      if (!completedSetRef.current.has(con.id)) {
        completedSetRef.current.add(con.id);
        const cp = project(con.center, camera, width, height);
        if (!cp.behind) {
          const [cr, cg, cb] = parseHex(con.color);
          for (let i = 0; i < 24; i++) {
            const angle = (i / 24) * Math.PI * 2 + (Math.random() - 0.5) * 0.3;
            const speed = 0.4 + Math.random() * 1.2;
            completionParticlesRef.current.push({
              x: cp.x, y: cp.y,
              vx: Math.cos(angle) * speed,
              vy: Math.sin(angle) * speed,
              birth: t, life: 3 + Math.random() * 2,
              size: 0.5 + Math.random() * 1.5,
              cr, cg, cb,
            });
          }
        }
      }
    }
    // Update and render completion particles
    const particles = completionParticlesRef.current;
    for (let i = particles.length - 1; i >= 0; i--) {
      const p = particles[i];
      const age = t - p.birth;
      if (age > p.life) {
        particles.splice(i, 1);
        continue;
      }
      p.x += p.vx;
      p.y += p.vy;
      p.vx *= 0.995;
      p.vy *= 0.995;
      const progress = age / p.life;
      const fadeIn = Math.min(1, progress * 8);
      const fadeOut = 1 - Math.pow(progress, 0.5);
      const alpha = fadeIn * fadeOut * 0.35 * globalA;
      const currentSize = p.size * (1 + progress * 0.5) * (1 - progress * 0.3);
      ctx.beginPath();
      ctx.arc(p.x, p.y, currentSize, 0, Math.PI * 2);
      ctx.fillStyle = `rgba(${p.cr},${p.cg},${p.cb},${alpha.toFixed(3)})`;
      ctx.fill();
      // Tiny glow halo
      if (alpha > 0.05) {
        const glowR = currentSize * 3;
        const glow = ctx.createRadialGradient(p.x, p.y, 0, p.x, p.y, glowR);
        glow.addColorStop(0, `rgba(${p.cr},${p.cg},${p.cb},${(alpha * 0.3).toFixed(4)})`);
        glow.addColorStop(1, `rgba(${p.cr},${p.cg},${p.cb},0)`);
        ctx.fillStyle = glow;
        ctx.beginPath(); ctx.arc(p.x, p.y, glowR, 0, Math.PI * 2); ctx.fill();
      }
    }

    // ── Constellation labels (progressive: only if touched) ──
    if (camera.zoom > 320) {
      for (const constellation of CONSTELLATIONS) {
        const hasLit = stars.some(s => s.constellation === constellation.id && s.illuminated);
        if (!hasLit) continue; // hidden until first illumination

        const cp = project(constellation.center, camera, width, height);
        if (cp.behind || cp.scale < 0.3) continue;
        const [cr, cg, cb] = parseHex(constellation.color);

        const litCount = stars.filter(s => s.constellation === constellation.id && s.illuminated).length;
        const totalCount = constellation.starIds.length;
        const completion = litCount / totalCount;

        // Label grows more visible as constellation is completed
        const labelA = (0.03 + completion * 0.04) * cp.scale * globalA;
        ctx.font = `500 ${Math.max(7, 8 * cp.scale)}px ${SANS}`;
        ctx.textAlign = 'center';
        ctx.fillStyle = `rgba(${cr},${cg},${cb},${labelA.toFixed(3)})`;

        // Use personal name if available, otherwise astronomical name
        const displayName = constellationNames[constellation.id] || constellation.name;
        ctx.fillText(displayName.toUpperCase(), cp.x, cp.y - 22 * cp.scale);

        // Subtle completion indicator
        if (completion > 0 && completion < 1) {
          const indicatorY = cp.y - 16 * cp.scale;
          const indicatorW = 20 * cp.scale;
          ctx.beginPath();
          ctx.moveTo(cp.x - indicatorW / 2, indicatorY);
          ctx.lineTo(cp.x - indicatorW / 2 + indicatorW * completion, indicatorY);
          ctx.strokeStyle = `rgba(${cr},${cg},${cb},${(labelA * 0.4).toFixed(3)})`;
          ctx.lineWidth = 0.3;
          ctx.stroke();
        }
      }
    }

    // ── Shooting star — frequency scales with illumination ──
    const shoot = shootRef.current;
    if (!shoot.active) {
      // Base: ~once every 10s. At full illumination: ~once every 3s
      // Phase F: meteor shower season dramatically increases spawn rate
      const meteorBonus = cosmicSeason?.effect === 'meteor_shower' ? cosmicSeason.intensity * 0.015 : 0;
      const spawnRate = 0.0017 + progress * 0.004 + meteorBonus;
      if (Math.random() < spawnRate && !dimmed) {
        shoot.active = true;
        shoot.x = Math.random() * width;
        shoot.y = Math.random() * height * 0.5;
        shoot.angle = Math.PI * 0.15 + Math.random() * Math.PI * 0.2;
        shoot.speed = 2.5 + Math.random() * 2 + progress * 1.5;
        shoot.birth = t;
        shoot.life = 0.6 + Math.random() * 0.5;
      }
    }
    if (shoot.active) {
      const age = t - shoot.birth;
      if (age > shoot.life) {
        shoot.active = false;
      } else {
        const progress = age / shoot.life;
        const fadeIn = Math.min(1, progress * 5);
        const fadeOut = Math.max(0, 1 - (progress - 0.5) * 3);
        const alpha = Math.min(fadeIn, fadeOut) * 0.25 * globalA;
        const tailLen = 30 + shoot.speed * 8;

        const cx = shoot.x + Math.cos(shoot.angle) * shoot.speed * age * 120;
        const cy = shoot.y + Math.sin(shoot.angle) * shoot.speed * age * 120;
        const tx = cx - Math.cos(shoot.angle) * tailLen;
        const ty = cy - Math.sin(shoot.angle) * tailLen;

        const grad = ctx.createLinearGradient(tx, ty, cx, cy);
        grad.addColorStop(0, `rgba(200,210,240,0)`);
        grad.addColorStop(0.7, `rgba(200,210,240,${(alpha * 0.3).toFixed(3)})`);
        grad.addColorStop(1, `rgba(220,225,245,${alpha.toFixed(3)})`);

        ctx.beginPath();
        ctx.moveTo(tx, ty);
        ctx.lineTo(cx, cy);
        ctx.strokeStyle = grad;
        ctx.lineWidth = 0.6;
        ctx.stroke();

        // Head point
        ctx.beginPath();
        ctx.arc(cx, cy, 0.5, 0, Math.PI * 2);
        ctx.fillStyle = `rgba(230,235,250,${(alpha * 1.5).toFixed(3)})`;
        ctx.fill();
      }
    }

    ctx.restore();
    rafRef.current = requestAnimationFrame(render);
  }, [stars, camera, width, height, breath, focusedStarId, dimmed, constellationNames, onTerritoryProximity, cosmicSeason, sessionStarIds, territoryBreaths, passageLuminances, afterglows, inscriptionRipples, passageTides, stardustMemories, harmonicPairs, gravityWells, passageWeaves]);

  useEffect(() => {
    rafRef.current = requestAnimationFrame(render);
    return () => cancelAnimationFrame(rafRef.current);
  }, [render]);

  return (
    <canvas
      ref={canvasRef}
      style={{ position: 'absolute', inset: 0, width: '100%', height: '100%', pointerEvents: 'none' }}
    />
  );
}

