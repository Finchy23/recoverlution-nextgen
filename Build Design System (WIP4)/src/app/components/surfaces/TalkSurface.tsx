// ═══════════════════════════════════════════════════
// SEED PROMPTS — the beginning of a thought already forming
// ═══════════════════════════════════════════════════
//
// Three short territory seeds (1-3 words) appear vertically centered.
// They feel like the user's own thought beginning to form,
// not questions from a clinical interface.
//
// Tap a seed → the observation unfolds → inscription.

const SEED_CYCLE_MS = 18000;
const SEED_STAGGER_MS = 600;
const CHAR_BASE_MS = 55;

function charDelay(ch: string): number {
  if (/[.…,;:]/.test(ch)) return CHAR_BASE_MS * 4.5;
  if (/[aeiouAEIOU]/.test(ch)) return CHAR_BASE_MS * 1.4;
  if (ch === ' ') return CHAR_BASE_MS * 1.8;
  return CHAR_BASE_MS + Math.random() * 12;
}

function typingDuration(text: string): number {
  let total = 0;
  for (const ch of text) total += charDelay(ch);
  return total;
}

function TypewriterPrompts({ observations, color, breath, onSelect, onCycle, opener, isFirstVisit }: {
  observations: Observation[];
  color: string;
  breath: number;
  onSelect: (obs: Observation) => void;
  onCycle: () => void;
  opener: string;
  isFirstVisit: boolean;
}) {
  const [selectedId, setSelectedId] = useState<string | null>(null);
  const [revealed, setRevealed] = useState<number[]>([0, 0, 0]);
  const [typingPhase, setTypingPhase] = useState<'typing' | 'holding' | 'dissolving'>('typing');
  const timerRef = useRef<ReturnType<typeof setTimeout>[]>([]);
  const cycleTimerRef = useRef<ReturnType<typeof setTimeout> | null>(null);
  const breathPhase = Math.sin(breath * Math.PI * 2);

  // ── Ceremony staging ──
  // Stage 0: nothing visible
  // Stage 1: opener fades in (ATMOSPHERE voice)
  // Stage 2: preamble (first visit only, INVITATION voice)
  // Stage 3: seeds begin typewriting
  const [ceremonyStage, setCeremonyStage] = useState<0 | 1 | 2 | 3>(0);
  const ceremonyTimersRef = useRef<ReturnType<typeof setTimeout>[]>([]);
  const ceremonyStartedForKeyRef = useRef<string>('');
  const hasCompletedCeremonyRef = useRef(false);

  // ── Territory seeds — short 1-3 word fragments ──
  // Computed once per observation set, stable across renders
  const seeds = useMemo(() => {
    const usedSeeds = new Set<string>();
    return observations.map(obs => pickSeed(obs.territory, usedSeeds));
  }, [observations]);

  const getConForObs = (obs: Observation) => CONSTELLATIONS.find(c => c.schema === obs.territory);

  const obsKey = observations.map(o => o.id).join(',');

  // ── Ceremony staging effect ──
  useEffect(() => {
    if (!obsKey || obsKey === ceremonyStartedForKeyRef.current) return;
    ceremonyStartedForKeyRef.current = obsKey;

    ceremonyTimersRef.current.forEach(clearTimeout);
    ceremonyTimersRef.current = [];
    setSelectedId(null);
    setRevealed([0, 0, 0]);
    setTypingPhase('typing');
    timerRef.current.forEach(clearTimeout);
    timerRef.current = [];
    if (cycleTimerRef.current) clearTimeout(cycleTimerRef.current);

    if (hasCompletedCeremonyRef.current) {
      setCeremonyStage(3);
      return;
    }

    setCeremonyStage(0);
    const t1 = setTimeout(() => setCeremonyStage(1), 400);
    ceremonyTimersRef.current.push(t1);

    if (isFirstVisit) {
      const t2 = setTimeout(() => setCeremonyStage(2), 2000);
      ceremonyTimersRef.current.push(t2);
      const t3 = setTimeout(() => {
        setCeremonyStage(3);
        hasCompletedCeremonyRef.current = true;
      }, 4200);
      ceremonyTimersRef.current.push(t3);
    } else {
      const t3 = setTimeout(() => {
        setCeremonyStage(3);
        hasCompletedCeremonyRef.current = true;
      }, 1800);
      ceremonyTimersRef.current.push(t3);
    }

    return () => { ceremonyTimersRef.current.forEach(clearTimeout); ceremonyTimersRef.current = []; };
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [obsKey, isFirstVisit]);

  // ── Seed typewriter effect — types short seeds, not long sentences ──
  useEffect(() => {
    if (ceremonyStage < 3) return;

    timerRef.current.forEach(clearTimeout);
    timerRef.current = [];

    let maxTypingEnd = 0;

    seeds.forEach((seed, i) => {
      const startDelay = 200 + i * SEED_STAGGER_MS;
      let charIdx = 0;

      const typeNextChar = () => {
        if (charIdx >= seed.length) return;
        const ch = seed[charIdx];
        charIdx++;
        const currentIdx = charIdx;
        setRevealed(prev => {
          const next = [...prev];
          next[i] = currentIdx;
          return next;
        });
        if (charIdx < seed.length) {
          const t = setTimeout(typeNextChar, charDelay(ch));
          timerRef.current.push(t);
        }
      };

      const dur = typingDuration(seed);
      if (startDelay + dur > maxTypingEnd) maxTypingEnd = startDelay + dur;

      const staggerTimer = setTimeout(typeNextChar, startDelay);
      timerRef.current.push(staggerTimer);
    });

    const holdTimer = setTimeout(() => {
      setTypingPhase('holding');
      cycleTimerRef.current = setTimeout(() => {
        setTypingPhase('dissolving');
        const dissolveTimer = setTimeout(() => { onCycle(); }, 800);
        timerRef.current.push(dissolveTimer);
      }, SEED_CYCLE_MS);
    }, maxTypingEnd + 600);
    timerRef.current.push(holdTimer);

    return () => {
      timerRef.current.forEach(clearTimeout);
      timerRef.current = [];
      if (cycleTimerRef.current) clearTimeout(cycleTimerRef.current);
    };
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [ceremonyStage, obsKey]);

  const handleSelect = (obs: Observation) => {
    if (selectedId) return;
    setSelectedId(obs.id);
    if (cycleTimerRef.current) clearTimeout(cycleTimerRef.current);
    timerRef.current.forEach(clearTimeout);

    const idx = observations.findIndex(o => o.id === obs.id);
    if (idx >= 0) {
      setRevealed(prev => {
        const next = [...prev];
        next[idx] = seeds[idx]?.length || 0;
        return next;
      });
    }
    setTimeout(() => onSelect(obs), 500);
  };

  const PREAMBLE = 'constellations of feeling hold space here... each star, an invitation for words not yet spoken';

  return (
    <div className="absolute inset-0" style={{ pointerEvents: 'none' }}>

      {/* ── Opener — ATMOSPHERE voice, persists as anchor ── */}
      <AnimatePresence>
        {ceremonyStage >= 1 && !selectedId && (
          <motion.div
            key="ceremony-opener"
            className="absolute pointer-events-none"
            style={{
              top: '22%', left: '10%', right: '10%',
              textAlign: 'center',
            }}
            initial={{ opacity: 0 }}
            animate={{
              opacity: ceremonyStage >= 3
                ? op.trace + breathPhase * 0.005
                : op.quiet + breathPhase * 0.01,
            }}
            exit={{ opacity: 0 }}
            transition={{ duration: 1.5, ease: [0.16, 1, 0.3, 1] }}
          >
            <span style={{
              fontFamily: SANS,
              fontSize: typeSize.label,
              fontWeight: weight.medium,
              letterSpacing: tracking.breath,
              textTransform: 'uppercase' as const,
              color: room.fg,
            }}>
              {opener}
            </span>
          </motion.div>
        )}
      </AnimatePresence>

      {/* ── Preamble — INVITATION voice (first visit only) ── */}
      <AnimatePresence>
        {ceremonyStage >= 2 && ceremonyStage < 3 && isFirstVisit && !selectedId && (
          <motion.div
            key="ceremony-preamble"
            className="absolute pointer-events-none"
            style={{
              top: '32%', left: '12%', right: '12%',
              textAlign: 'center',
            }}
            initial={{ opacity: 0, y: 6 }}
            animate={{ opacity: op.spoken + breathPhase * 0.02, y: 0 }}
            exit={{ opacity: 0 }}
            transition={{ duration: 1.8, ease: [0.16, 1, 0.3, 1] }}
          >
            <span style={{
              fontFamily: SERIF,
              fontSize: typeSize.lede,
              fontWeight: weight.light,
              fontStyle: 'italic',
              color: room.fg,
              letterSpacing: tracking.body,
              lineHeight: leading.generous,
            }}>
              {PREAMBLE}
            </span>
          </motion.div>
        )}
      </AnimatePresence>

      {/* ── Seeds — short territory fragments, vertically centered ── */}
      {/* These feel like the user's own thought forming, not prompts from the platform */}
      <AnimatePresence>
        {ceremonyStage >= 3 && !selectedId && (
          <motion.div
            key="seed-column"
            className="absolute pointer-events-none"
            style={{
              top: 0, left: 0, right: 0, bottom: 0,
              display: 'flex', flexDirection: 'column',
              alignItems: 'center', justifyContent: 'center',
              gap: 0,
              paddingBottom: '8%',
            }}
            initial={{ opacity: 0 }}
            animate={{ opacity: typingPhase === 'dissolving' ? 0 : 1 }}
            exit={{ opacity: 0 }}
            transition={{ duration: typingPhase === 'dissolving' ? 0.8 : 0.6 }}
          >
            {observations.map((obs, i) => {
              const con = getConForObs(obs);
              const territoryColor = con?.color || color;
              const conName = con?.name || '';
              const seed = seeds[i] || '';
              const chars = revealed[i] || 0;
              const visibleText = seed.slice(0, chars);
              const isFullyTyped = chars >= seed.length;
              const showCursor = chars > 0 && !isFullyTyped;
              const driftY = Math.sin(breath * Math.PI * 2 + i * 2.1) * 2;

              return (
                <motion.div
                  key={obs.id}
                  style={{
                    display: 'flex', flexDirection: 'column',
                    alignItems: 'center', gap: 0,
                    padding: '14px 24px',
                  }}
                  initial={{ opacity: 0 }}
                  animate={{
                    opacity: chars > 0 ? 1 : 0,
                    y: driftY,
                  }}
                  transition={{
                    opacity: { duration: 0.6 },
                    y: { duration: 4, ease: 'easeInOut' },
                  }}
                >
                  {/* Constellation name whisper — ATMOSPHERE voice, territory-colored, trace opacity */}
                  {conName && isFullyTyped && (
                    <motion.span
                      className="pointer-events-none"
                      initial={{ opacity: 0 }}
                      animate={{ opacity: op.trace + breathPhase * 0.003 }}
                      transition={{ duration: 1.2 }}
                      style={{
                        fontFamily: SANS,
                        fontSize: typeSize.whisper,
                        fontWeight: weight.medium,
                        letterSpacing: tracking.breath,
                        textTransform: 'uppercase' as const,
                        color: territoryColor,
                        marginBottom: 4,
                      }}
                    >
                      {conName}
                    </motion.span>
                  )}

                  {/* Seed button */}
                  <motion.button
                    className="cursor-pointer bg-transparent border-none outline-none"
                    style={{
                      padding: '4px 0',
                      textAlign: 'center',
                      pointerEvents: chars > 1 ? 'auto' : 'none',
                      position: 'relative',
                    }}
                    animate={{
                      opacity: chars > 0 ? op.voice + breathPhase * 0.03 : 0,
                    }}
                    whileHover={{ opacity: op.strong }}
                    transition={{ opacity: { duration: 0.4 } }}
                    onClick={() => handleSelect(obs)}
                  >
                    {/* Seed text — INVITATION voice, serif italic, single color (room.fg) */}
                    <span style={{
                      fontFamily: SERIF,
                      fontSize: typeSize.prose,
                      fontWeight: weight.light,
                      fontStyle: 'italic',
                      color: room.fg,
                      letterSpacing: tracking.body,
                      lineHeight: leading.relaxed,
                      position: 'relative',
                    }}>
                      {visibleText}
                      {showCursor && (
                        <span
                          style={{
                            display: 'inline-block',
                            width: 1,
                            height: '1em',
                            backgroundColor: room.fg,
                            marginLeft: 2,
                            verticalAlign: 'text-bottom',
                            opacity: 0.3 + breathPhase * 0.15,
                            animation: 'typewriterCursorBlink 1s ease-in-out infinite',
                          }}
                        />
                      )}
                    </span>
                  </motion.button>
                </motion.div>
              );
            })}
          </motion.div>
        )}
      </AnimatePresence>
    </div>
  );
}

