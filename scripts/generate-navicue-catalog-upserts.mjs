#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';

function parseCsv(raw) {
  const rows = [];
  let i = 0;
  let field = '';
  let row = [];
  let inQuotes = false;

  while (i < raw.length) {
    const c = raw[i];
    const next = raw[i + 1];

    if (inQuotes) {
      if (c === '"' && next === '"') {
        field += '"';
        i += 2;
        continue;
      }
      if (c === '"') {
        inQuotes = false;
        i += 1;
        continue;
      }
      field += c;
      i += 1;
      continue;
    }

    if (c === '"') {
      inQuotes = true;
      i += 1;
      continue;
    }

    if (c === ',') {
      row.push(field);
      field = '';
      i += 1;
      continue;
    }

    if (c === '\n') {
      row.push(field);
      rows.push(row);
      row = [];
      field = '';
      i += 1;
      continue;
    }

    if (c === '\r') {
      i += 1;
      continue;
    }

    field += c;
    i += 1;
  }

  if (field.length > 0 || row.length > 0) {
    row.push(field);
    rows.push(row);
  }

  return rows;
}

function sqlText(v) {
  if (v === null || v === undefined) return 'null';
  const s = String(v).trim();
  if (!s) return 'null';
  return `'${s.replace(/'/g, "''")}'`;
}

const [csvPathArg, outPathArg] = process.argv.slice(2);
if (!csvPathArg) {
  console.error('Usage: node scripts/generate-navicue-catalog-upserts.mjs <scaffold.csv> [output.sql]');
  process.exit(1);
}

const csvPath = path.resolve(csvPathArg);
if (!fs.existsSync(csvPath)) {
  console.error(`CSV not found: ${csvPath}`);
  process.exit(1);
}

const raw = fs.readFileSync(csvPath, 'utf8');
const matrix = parseCsv(raw);
if (matrix.length < 2) {
  console.error('CSV has no data rows.');
  process.exit(1);
}

const headers = matrix[0].map(h => h.trim());
const dataRows = matrix.slice(1).filter(r => r.some(v => String(v || '').trim().length));

const idx = {
  cue_number: headers.indexOf('cue_number'),
  stream_name: headers.indexOf('stream_name'),
  block_name: headers.indexOf('block_name'),
  block_number: headers.indexOf('block_number'),
  navicue_type_id: headers.indexOf('navicue_type_id')
};

if (idx.cue_number === -1 || idx.stream_name === -1 || idx.block_name === -1 || idx.block_number === -1) {
  console.error('CSV must include: cue_number, stream_name, block_name, block_number');
  process.exit(1);
}

const streams = new Map();
const blocks = new Map();
const cues = [];

for (const r of dataRows) {
  const cueNumber = Number((r[idx.cue_number] || '').trim());
  if (!Number.isFinite(cueNumber) || cueNumber <= 0) continue;

  const streamName = (r[idx.stream_name] || '').trim() || 'Unassigned Stream';
  const streamCode = streamName.toLowerCase().replace(/[^a-z0-9]+/g, '_').replace(/^_+|_+$/g, '');
  streams.set(streamCode, { streamCode, streamName });

  const blockNumber = Number((r[idx.block_number] || '').trim()) || 1;
  const blockName = (r[idx.block_name] || '').trim() || `Block ${blockNumber}`;
  const blockKey = `${streamCode}::${blockNumber}`;
  blocks.set(blockKey, { streamCode, blockNumber, blockName });

  const navicueTypeId = idx.navicue_type_id === -1 ? '' : (r[idx.navicue_type_id] || '').trim();
  const cueSlug = `cue_${cueNumber}`;
  const cueKey = `cue_${cueNumber}`;

  cues.push({ cueNumber, streamCode, blockNumber, blockName, navicueTypeId, cueSlug, cueKey });
}

const lines = [];
lines.push('-- Generated by scripts/generate-navicue-catalog-upserts.mjs');
lines.push('begin;\n');

for (const s of streams.values()) {
  lines.push(
    `insert into public.navicue_streams (stream_code, stream_name) values (${sqlText(s.streamCode)}, ${sqlText(s.streamName)}) on conflict (stream_code) do update set stream_name = excluded.stream_name;`
  );
}

lines.push('');

for (const b of blocks.values()) {
  lines.push(
    `insert into public.navicue_blocks (stream_id, block_number, block_name)\n` +
    `select s.id, ${b.blockNumber}, ${sqlText(b.blockName)}\n` +
    `from public.navicue_streams s\n` +
    `where s.stream_code = ${sqlText(b.streamCode)}\n` +
    `on conflict (stream_id, block_number) do update set block_name = excluded.block_name;`
  );
}

lines.push('');

for (const c of cues) {
  lines.push(
    `insert into public.navicue_cues (cue_key, cue_number, stream_id, block_id, navicue_type_id, cue_title, cue_slug, lifecycle_status, is_active)\n` +
    `select ${sqlText(c.cueKey)}, ${c.cueNumber}, s.id, b.id, ${sqlText(c.navicueTypeId) || 'null'}, ${sqlText(`Cue ${c.cueNumber}`)}, ${sqlText(c.cueSlug)}, 'draft', true\n` +
    `from public.navicue_streams s\n` +
    `join public.navicue_blocks b on b.stream_id = s.id and b.block_number = ${c.blockNumber}\n` +
    `where s.stream_code = ${sqlText(c.streamCode)}\n` +
    `on conflict (cue_key) do update set\n` +
    `  cue_number = excluded.cue_number,\n` +
    `  stream_id = excluded.stream_id,\n` +
    `  block_id = excluded.block_id,\n` +
    `  navicue_type_id = coalesce(excluded.navicue_type_id, public.navicue_cues.navicue_type_id),\n` +
    `  updated_at = now();`
  );
}

lines.push('\ncommit;');

const output = lines.join('\n');
const outPath = outPathArg ? path.resolve(outPathArg) : path.resolve('services/supabase/migrations/generated_navicue_catalog_upserts.sql');
fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, output, 'utf8');

console.log(`Generated SQL: ${outPath}`);
console.log(`Streams: ${streams.size} | Blocks: ${blocks.size} | Cues: ${cues.length}`);
